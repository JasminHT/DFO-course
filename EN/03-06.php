<p>Given the use of statistical techniques, making assumptions, and the general incompleteness of knowledge about natural systems, all stock assessments inherently involve some level of uncertainty. Scientists make logical and defensible <span class="gc-dwnld-info"><abbr title="Assumption: a statement introduced into an argument that is accepted as being true.">assumptions</abbr></span> about stocks during stock assessments. It is critical for scientists to communicate uncertainty in order to provide managers with advice for decision making.
</p>

<p>Uncertainty does not mean an error has been made. It means that there is a range of possible outcomes, some of which are more likely to happen than others. It may also mean there was not enough data to parse out plausible outcomes.</p>


    <p>Potential sources of uncertainty include: </p>

    <ol class='lst-spcd'>
      <li>natural variability in population dynamics (for example, different levels of recruitment between years)</li>
      <li>the population dynamics used in modelling (for example, predator-prey relationships, stock-recruitment relationships, environmental and climate factors) are not understood</li>
      <li>missing data (for example, unreported catch)</li>
      <li>measurement and sampling error</li>
      <li>the extent to which management policies will be successfully implemented is not known (for example, how well catch will match total allowable catch projections, changes to policy)</li>
    </ol>


<p>DFO incorporates uncertainty into the calculation of <span class="gc-dwnld-info"><abbr title="Stock status: refers to a determination made, on the basis of stock assessment results, about the current condition of the stock.">stock status</abbr></span>  and <span class="gc-dwnld-info"><abbr title="Reference point: a benchmark against which the biomass or abundance of the stock or the fishing mortality rate (or exploitation rate) can be measured in order to determine its status. These reference points can be targets, thresholds or limits depending on their intended use.">reference points</abbr></span>. </p>

<aside class='bg-warning'>
  <h4>Key concept:</h4>
  <p>Recall: DFO takes a <a href="https://www.dfo-mpo.gc.ca/reports-rapports/regs/sff-cpd/precaution-back-fiche-eng.htm"><strong>precautionary approach</strong></a> to fisheries management. This means being cautious when scientific information is uncertain, unreliable, or inadequate and not using the absence of adequate scientific information as a reason to postpone or fail to take action to avoid <span class="gc-dwnld-info"><abbr title="Serious harm: a fish stock experiencing serious harm may be depleted to the point where it has impaired productivity or reproductive capacity. As a result, the stock would be less resilient to fishing and could not easily rebuild. Serious harm may be irreversible, or only slowly reversible over the long-term, and can be due to fishing, other human-induced impacts, or natural causes.">serious harm</abbr></span> to a resource.</p>
