<h2>Unit 3.2: Management Strategy Evaluation</h2>

<figure>
  <img src="../images/airplane-pilots.jpg">
  <figcaption>Photo credit: Shutterstock</figcaption>
</figure>


 


<p><b>Management Strategy Evaluation (MSE)</b> provides a structured approach to sustainable fisheries management. MSE is like a flight simulator for fisheries: it tests different fishing scenarios to figure out how to catch fish sustainably, similar to pilots practicing flying.</p>

<p>Imagine an airplane pilot: their goal is to safely reach their destination, even if there is turbulence and inclement weather. For MSE:</p>

<ul>
  <li>the “plane” is the fishery management strategy</li>
<li>“weather conditions” are the fish stock, fishery, and environment </li>
<li>reaching the “destination” is achieving pre-determined objectives for the fishery</li></ul>

<p>MSE can be applied across a range of species, life histories, <span class="gc-dwnld-info"><abbr title="Stock status: refers to a determination made, on the basis of stock assessment results, about the current condition of the stock.">stock statuses</abbr></span>, and data availability levels, and include input from <span class="gc-dwnld-info"><abbr title="Rights-holders: modern successors to the Indigenous collectives that signed treaties, occupied and used land, and engaged in historic practices that support a finding of an Aboriginal right or Aboriginal title.">Rights-holders</abbr></span> and stakeholders.</p>


<p>Here is how MSE works:</p>

<div class='collapsible blue' id="part1">
  <details>
    <summary>1. Define the decision context and set objectives for the fishery</summary>
    <p>Key questions to guide defining the decision context include: </p>
    <ul>
    <li>What is the decision to be made?</li>
    <li>What is the time frame for making the decision?</li>
    <li>How often will the decision be evaluated and updated?</li>
    <li>What are the legislative and policy requirements?</li>
    <li>What are the specific roles and responsibilities of parties involved? </li>
    </ul>

    <p>An example of an objective for a pilot could be to land the plane with half a tank of fuel left. Examples of MSE objectives could be to keep the fish stock above its <span class="gc-dwnld-info"><abbr title="Limit reference point (LRP): the stock status below which serious harm is occurring to the stock.">limit reference point</abbr></span> or to catch a certain amount of fish each year. </p>

    <p>If multiple objectives are set for a fishery, it may not be possible to meet them all at the same time. Trade-offs may be required.</p>

  </details>
</div>

<div class='collapsible blue' id="part2">
  <details>
    <summary>2. Create operating models </summary>
    <p><b>Operating models</b> are computer models that simulate how fish stocks grow, reproduce, and interact with the environment. They can simulate environmental changes over time and how various aspects of a fishery may affect stocks, such as fishing in specific locations or with different types of gear.</p>  

    <p>Just like weather conditions in a flight simulator, the interactions between fish stocks, fisheries, and the environment vary over time and are not easy to predict. More than one operating model may be developed to represent these different scenarios.</p>
  </details>
</div>

<div class='collapsible blue' id="part3">
  <details>
    <summary>3. Test different management strategies </summary>
    <p>Pilots practice flying their planes in different flight conditions, like turbulence and good or bad weather. This allows them to try different flight paths or altitudes to see how much fuel is left upon landing. </p>

    <p>For MSE, management strategies are tested by adjusting factors like catch limits, fishing seasons, or gear types to see how well these different scenarios achieve fisheries objectives.</p>
  </details>
</div>

<div class='collapsible blue' id="part4">
  <details>
    <summary>4. Evaluate the impact of different management strategies</summary>
    <p>After running simulations, it is possible to see how well management strategies meet the objectives set in step 1. </p>

    <p>In a flight simulator, pilots use different flight paths to measure how their choices affect performance metrics, such as fuel use and arriving safely and on time. Similarly, the amount of catch or a stock status can be used as performance metrics for MSE.</p>
  </details>
</div>

<div class='collapsible blue' id="part5">
  <details>
    <summary>5. Choose and implement a management strategy </summary>
    <p>The management strategy that best meets objectives in simulations can be put into practice. However, deciding what is “best” may not be easy. Different management strategies will achieve some objectives better than others, so there may be trade-offs. For example, management strategies more likely to achieve high catch may also be more likely to risk fish stocks depleting below their limit reference point. </p>

    <p>Fishery managers need to consider how important different objectives are and what trade-offs and risks are acceptable when selecting a management strategy.</p>

    <p>Once implemented, the chosen management strategy should be monitored to ensure it is working well in the real world.</p>
  </details>
</div>

<div class='collapsible blue' id="part6">
  <details>
    <summary>6. Repeat to incorporate new information </summary>
    <p>MSE is an iterative and adaptive process. The outcomes of management action can inform refinements to fishery objectives and management strategies. In this way, fisheries management is improved over time and adapted as knowledge accumulates. Ongoing engagement with Rights-holders and stakeholders may be required.</p>
  </details>
</div>


