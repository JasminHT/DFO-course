<div class='collapsible blue' id="module1">
  <details>
    <summary><strong>Abundance</strong></summary>
    <p><strong>Abundance (N)</strong> 
    is the total number of fish in a stock. </p>
  </details>
</div>

<div class='collapsible blue' id="module2">
  <details>
    <summary><strong>Biomass</strong></summary>
    <p><strong>Biomass (B) </strong> 
    is the total weight of all fish in a stock. </p>
  </details>
</div>


<div class='collapsible blue' id="module3">
  <details>
    <summary><strong>Productivity</strong></summary>
    <p><strong>Productivity</strong> 
     is the rate at which biomass is generated in a stock.</p>
  </details>
</div>


<div class='collapsible blue' id="module4">
  <details>
    <summary><strong>Spawning stock biomass</strong></summary>
    <p><strong>Spawning stock biomass (SSB)</strong> 
     is the total weight of all fish in a stock that have reached reproductive maturity.</p>
  </details>
</div>


<div class='collapsible blue' id="module5">
  <details>
    <summary><strong>Recruitment</strong></summary>
    <p><strong>Recruitment</strong>  
     occurs when new fish are added to a population. It is commonly measured when small or young fish grow large or old enough to be caught in a fishery (which may be affected by catch limits or gear type) or scientific survey.  </p>
  </details>
</div>


<div class='collapsible blue' id="module6">
  <details>
    <summary><strong>Cohort</strong></summary>
    <p>A <strong>cohort</strong> 
     or year-class is all fish in a stock born in the same spawning year. The amount of recruitment determines the strength (in other words, relative abundance) of a cohort.</p>
  </details>
</div>




<div class='collapsible blue' id="key2">
  <details>
    <summary><strong>Fishing mortality</strong></summary>
    <p><strong>Fishing mortality (F)</strong> is the measure of deaths in a stock due to all fishing activities, including:</p>
  <ul>
    <li>targeted fishing</li>
    <li>  <span class="gc-dwnld-info"><abbr title="Bycatch: the unintentional catch of a species when meaning to catch another. The retained portion of bycatch is called incidental catch, whereas the portion returned to the water is called discards.">bycatch</abbr></span> and discards</li>
    <li>other incidental mortality in other fisheries</li>
  </ul> 
  </details>
</div>

<div class='collapsible blue' id="key3">
  <details>
    <summary><strong>Natural mortality</strong></summary>
    <p><strong>Natural mortality (M)</strong> is a measure of deaths in a stock due to everything other than fishing, such as:         </p>
      <ul>
      <li>predation</li>
      <li>disease</li>
      <li>prey availability</li>
      <li>competition for resources </li>
      </ul>

  </details>
</div>

<div class='collapsible blue' id="key4">
  <details>
    <summary><strong>Fishing effort</strong></summary>
    <p><strong>Fishing effort</strong> is a measure of fishing activity using metrics such as: </p>
    <ul> 
      <li>the number of nets or lines used</li>
      <li>the number of fishing vessels deployed</li>
      <li>the number of hours spent fishing </li>
    </ul>
    
  </details>
</div>

<div class='collapsible blue' id="key5">
  <details>
    <summary><strong>Catchability</strong></summary>
    <p><strong>Catchability</strong> is a concept that reflects the efficiency of a fishery. It is the combination of <strong>availability</strong> (the likelihood that a fish will enter fishing gear) and <strong>selectivity</strong> (the likelihood of retention once a fish enters fishing gear). Gear selectivity is affected by gear design, such as mesh type and size, escape panels, and hook size.</p>
    
  </details>
</div>

