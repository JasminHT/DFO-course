<p>All credible ecosystem and multispecies approaches are built upon decades of peer reviewed single species work. All three approaches have strengths and weaknesses that contribute collectively to an improved understanding of fisheries.</p>

<p>DFO is moving towards an ecosystem approach to fisheries management. It will:</p>

<ul>
  <li>promote a sustainable, economically viable, internationally competitive fishing industry</li>
  <li>improve the management of Canadian fisheries by enabling more informed decision making</li>
  <li>align with the global shift towards ecosystem approaches to fisheries management</li>
  <li>help respond to the challenges of climate change and other important ecosystem-level changes on fish stocks</li>
  <li>help meet policy obligations, international commitments, eco-certification requirements, and international trade requirements for sustainable fisheries</li>
  <li>respond to consumer demand for sustainable fisheries</li>
</ul>








