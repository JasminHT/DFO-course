<h2> Unit 1.2: What is stock assessment? </h2>
<p><strong>Stock-recruitment relationships</strong> are often important to stock assessments. They are the relationships between the <span class="gc-dwnld-info"><abbr title="Abundance (N): total number of fish in a stock.">abundance</abbr></span> or <span class="gc-dwnld-info"><abbr title="Biomass (B): the total weight of all fish in a stock.">biomass</abbr></span> of spawning adults in a stock and the expected number of recruits they can produce each year. In other words, the number of young fish in a stock depends on the number of mature fish that can reproduce each year. 
Where possible, these relationships can help determine the extent to which a fish stock can be harvested (or, how many fish are available to catch and how many should be left in the water to reproduce). </p>

