<h2>Unit 1.3:  Stock status and reference points</h2>

    <p>The <a href="https://www.dfo-mpo.gc.ca/reports-rapports/regs/sff-cpd/precaution-eng.htm">Precautionary Approach Policy</a> defines 3 stock status zones to categorize stock health: </p>
    <ul>
      <li >healthy</li>
      <li >cautious</li>
      <li >critical </li>
    </ul>

    <p>These zones are created by defining <strong>reference points</strong> that act as boundaries between zones.  Stock status can be used to:</p>
    <ul>
      <li>inform total allowable catch</li>
      <li>drive harvest decision rules</li>
      <li>evaluate how well harvest strategies are working</li> 
    </ul>

    <p>Reference points are used as targets to achieve or as limits to avoid. There are many different methods for estimating reference points that reflect:</p>
    <ul>
      <li>data availability</li>
      <li>biological knowledge of the species</li>
      <li><span class="gc-dwnld-info"><abbr title="Models are tools used to simplify a natural system so it can be more easily understood.">model</abbr></span> availability</li>
    </ul>

    <p>Reference points can be updated as:</p>
    <ul>
      <li>new data become available</li>
      <li>new <span class="gc-dwnld-info"><abbr title="Models are tools used to simplify a natural system so it can be more easily understood.">models</abbr></span> are developed</li>
      <li>stocks experience changes in productivity</li> 
    </ul>

    <h3>Reference point definitions</h3>

    <div class='collapsible blue' id="module1">
      <details>
        <summary><strong>Limit reference point</strong></summary>
        <p><strong>Limit reference point (LRP)</strong> is the stock status below which serious harm is occurring to the stock. There may also be impacts to the ecosystem, other species, and long-term loss of fishing opportunities.  
         </p>

         <aside class='bg-info'>
          <h4>Definition: </h4>
          <p>A fish stock experiencing <b>serious harm</b> may be depleted to the point where it has impaired productivity or reproductive capacity. As a result, the stock would be less resilient to fishing and could not easily rebuild. Serious harm may be irreversible, or only slowly reversible over the long-term, and can be due to fishing, other human-induced impacts, or natural causes.</p></aside>
      </details>
    </div>

        <div class='collapsible blue' id="module2">
      <details>
        <summary><strong>Upper stock reference point</strong></summary>
        <p><strong>Upper stock reference point (USR)</strong> is a threshold below which fishing must be reduced to avoid reaching the LRP. The USR must be set at an appropriate distance above the LRP to provide sufficient opportunity for managers to recognize a declining stock status and sufficient time for management actions to take effect. </p>
      </details>
    </div>

        <div class='collapsible blue' id="module3">
      <details>
        <summary><strong>Target reference point</strong></summary>
        <p><strong>Target reference point (TRP)</strong> is the overall stock level target. It is determined by setting <span class="gc-dwnld-info"><abbr title="Productivity: the rate of generation of biomass in a stock.">productivity</abbr></span>, broader ecological, and socio-economic objectives for the fishery. It is typically set at or above the USR. </p>
      </details>
    </div>



        <div class='collapsible blue' id="module4">
      <details>
        <summary><strong>Removal reference</strong></summary>
        <p><strong>Removal reference</strong> is the maximum removal rate of a fish stock. It is less than or equal to the stock’s <span class="gc-dwnld-info"><abbr title="Maximum sustainable yield (MSY): the maximum average annual catch that can be removed from a stock over an indefinite period of time under current environmental conditions.">maximum sustainable yield</abbr></span>. It can be expressed in terms of: </p>
        <ul>
        <li>  <span class="gc-dwnld-info"><abbr title="Fishing mortality (F): measure of deaths in a stock due to all fishing activities, including targeted fishing, bycatch and discards, and other incidental mortality in other fisheries.">fishing mortality</abbr></span></li>
        <li><span class="gc-dwnld-info"><abbr title="Harvest rate: the proportion of the recruited or vulnerable biomass that is caught during a certain period, usually a fishing year (also referred to as exploitation rate).">harvest rate</abbr></span> </li>
        <li>  <span class="gc-dwnld-info"><abbr title="Fishing effort: a measure of fishing activity using metrics such as the number of nets or lines used, the number of fishing vessels deployed, the number of hours spent fishing, etc.">fishing effort</abbr></span></li>
        </ul>
        
      </details>
    </div>

