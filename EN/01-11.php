<div class='collapsible blue' id="module1">
  <details>
    <summary><strong>Steepness or recruitment compensation</strong></summary>
    <p><strong>Steepness or recruitment compensation</strong> is a measure of juvenile fish survival when there is little or no competition for resources (for example, food or habitat). </p>
  </details>
</div>

<div class='collapsible blue' id="module2">
  <details>
    <summary><strong>Spawning potential ratio </strong></summary>
    <p><strong>Spawning potential ratio </strong>is the number of eggs produced by a fish stock being fished relative to number of eggs that would be produced if it is not fished. </p>
  </details>
</div>


<div class='collapsible blue' id="module3">
  <details>
    <summary><strong>Yield per recruit</strong></summary>
    <p><strong>Yield per recruit</strong> is the expected lifetime catch in weight produced per recruited fish in the stock.</p>
  </details>
</div>


<div class='collapsible blue' id="module4">
  <details>
    <summary><strong>Unfished biomass </strong></summary>
    <p><strong>Unfished biomass </strong>is the total weight of fish that would exist in a stock if it is not fished. </p>
  </details>
</div>


<div class='collapsible blue' id="module5">
  <details>
    <summary><strong>Unfished recruitment </strong></summary>
    <p><strong>Unfished recruitment </strong>is the expected recruitment if the fish stock is not fished. </p>
  </details>
</div>


<div class='collapsible blue' id="module6">
  <details>
    <summary><strong>Depletion </strong></summary>
    <p> <strong>Depletion</strong> is a measure of the current biomass of a stock relative to the average unfished biomass. </p>
  </details>
</div>


