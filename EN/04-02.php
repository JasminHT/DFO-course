<p>Science advice is generated from stock assessments to inform decision making. Science advice may include information on <span class="gc-dwnld-info"><abbr title="Stock status: refers to a determination made, on the basis of stock assessment results, about the current condition of the stock.">stock status</abbr></span>  and the potential consequences of taking different management actions to sustain or rebuild the stock as needed. Fisheries management decisions are made by integrating the science advice with:</p>
<ul>
  <li>socio-economic considerations</li>
  <li>fishery policies</li>
  <li><span class="gc-dwnld-info"><abbr title="Rights-holders: modern successors to the Indigenous collectives that signed treaties, occupied and used land, and engaged in historic practices that support a finding of an Aboriginal right or Aboriginal title.">Rights-holder</abbr></span>  and stakeholder engagement</li>
  <li>Indigenous Ways of Knowing</li>
  <li>other cultural considerations</li>
</ul>

<p>The <a href="https://www.dfo-mpo.gc.ca/csas-sccs/index-eng.htm">Canadian Stock Assessment Secretariat</a> was formed in 1997 to provide advice related to stock status and trends in support of management of commercial fisheries. In 2001, the Secretariat was renamed to the Canadian Science Advisory Secretariat (CSAS) and is responsible for coordinating the development of DFOâ€™s peer-reviewed science advice. The high-quality science developed through the CSAS peer review process ensures that consultations with Rights-holders, stakeholders, and advisory bodies proceed from a shared and reliable information base. Scientific integrity and objectivity are critical elements of all stages of the CSAS process, including: </p>
<ul>
  <li>planning</li>
  <li>development of scientific documents</li>
  <li>peer review meetings</li>
  <li>dissemination of science advice</li>
</ul>

<figure>
  <img src="../images/fingers-on-keyboard.png">
