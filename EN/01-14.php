  <li>Fisheries management aims to promote the sustainable harvest of fish <span class="gc-dwnld-info"><abbr title="Stock: groups of the same species with a shared characteristic(s) of interest to managers.">stocks</abbr></span> and maintain healthy ecosystems. This is supported by sound science advice and the <span class="gc-dwnld-info"><abbr title="Precautionary approach: Being cautious when scientific information is uncertain, unreliable, or inadequate and not using the absence of adequate scientific information as a reason to postpone or fail to take action to avoid serious harm to a resource.">precautionary approach</abbr></span>. </li> 

  <li>In Canada, <span class="gc-dwnld-info"><abbr title="Fishery: the sum of all fishing activity on a resource.">fisheries</abbr></span> are managed by the department of Fisheries and Oceans Canada (DFO) for the benefit of all Canadians. As a science-based department, scientific integrity is essential to the work of DFO and its employees. </li>

  <li><span class="gc-dwnld-info"><abbr title="Rights-holders: modern successors to the Indigenous collectives that signed treaties, occupied and used land, and engaged in historic practices that support a finding of an Aboriginal right or Aboriginal title.">Rights-holders</abbr></span>  and stakeholders contribute significantly to fisheries science in Canada. </li>

  <li>Where possible, stock assessments project how fish stocks will react to different management actions. Scientists can provide advice to managers about the risks and uncertainty associated with taking different management actions.</li>

  <li>Stock assessments estimate the status of a stock. Healthy, cautious, and critical status zones are created by defining reference points that act as boundaries between them. </li>

  <ul>
    <li>The limit reference point (LRP) is the stock status below which <span class="gc-dwnld-info"><abbr title="Serious harm: a fish stock experiencing serious harm may be depleted to the point where it has impaired productivity or reproductive capacity. As a result, the stock would be less resilient to fishing and could not easily rebuild. Serious harm may be irreversible, or only slowly reversible over the long-term, and can be due to fishing, other human-induced impacts, or natural causes.">serious harm</abbr></span>  is occurring to the stock. </li>

    <li>The upper stock reference (USR) is the threshold below which fishing must be reduced to avoid reaching the LRP. </li>

    <li>The removal reference is the maximum removal rate of a fish stock. </li>

    <li>The target reference point (TRP) is the overall stock level target.</li>
  </ul>

  <li>A common approach used to define reference points is to estimate them based on a stock’s maximum sustainable yield. Indicators are often used to describe what is known about a stock’s status.</li>

</ul>

