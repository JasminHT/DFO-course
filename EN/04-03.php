<h2>Unit 4.1: Canadian Science Advisory Secretariat</h2>

<figure class='image-transcript'>
  <img src="../images/CSAS_EnsuresThatDFO.jpg?v=1" alt='4 key characteristics that describe DFO’s science advice: credible, objective, inclusive, transparent'>
  <figcaption>
    <details id="inline-captions">
      <summary>Long description</summary>      
      <p>CSAS ensures that DFO’s scientific advice is:</p>
      <ul>
        <li><strong>consistent:</strong> applying a high standard of technical evaluation to all science data and analyses</li>
        <li><strong>objective:</strong> reducing bias in the interpretation of results</li>
        <li><strong>inclusive:</strong> engaging a diverse group of experts from a variety of departments and organizations both domestic and international</li>
        <li><strong>transparent:</strong> publishing a calendar of CSAS meetings, science advice, and their supporting documents</li>
      </ul>
    </details>
  </figcaption>
</figure>



<p>The <b>CSAS Guiding Principles</b> provide direction to ensure that government decisions are informed by sound science advice. They are the basis of DFO’s peer review process: </p>
<ol>
  <li>  provide timely and responsive advice that is flexible to client needs</li>
  <li>  employ the most appropriate and credible scientific methods</li>
  <li>  provide accessible public document trails</li>
  <li>  involve a range of expertise and perspectives along with scientific experts from within and external to DFO in the review process</li>
</ol>

<p>CSAS peer review processes can be:</p>
<ul>
  <li>regional, addressing issues that affect a single DFO region</li>
  <li>zonal, addressing issues that affect more than 1 DFO region</li>
  <li>national, addressing issues that affect all of Canada</li>
</ul>

