<h2>Unit 4.2: Participating in peer review meetings</h2>

<figure>
  <img src="../images/table-meeting.jpg">
  <figcaption>Photo credit: Shutterstock</figcaption>
</figure>

<p>Inclusive participation in peer review meetings generates a full and open discussion. It brings conflicting viewpoints to the table and draws in scientific findings that may not otherwise be considered. Participation in CSAS peer review meetings is by non-transferrable invitation only to experts. </p>

<p>Experts may include participants from:</p>
<ul>
<li>DFO</li>
<li>other federal, provincial, or territorial government departments</li>
<li>Indigenous Nations, communities or organizations</li>
<li>industry</li>
<li>academia</li>
<li>non-government organizations (NGOs)</li>
<li>international organizations</li>
</ul>

<p>Steering Committees must ensure that the size of participant lists are manageable for the format of the meeting to achieve a thorough discussion. The final participants list should reflect:</p>
<ul>
<li>the nature of the issue</li>
<li>expertise required</li>
<li>diversity of scientific opinions on the topic</li>
<li>local and Indigenous Knowledge Holders as appropriate</li>
</ul>

<p>Participants must have the expertise required to:</p>
<ul>
<li>engage constructively in questions</li>
<li>comment on and challenge the scientific information and interpretations presented</li>
</ul>

<p>Such expertise may include:</p>
<ul>
<li>demonstrable, comprehensive, and authoritative scientific knowledge or related background and working experience </li>
<li>local knowledge and Indigenous Ways of Knowing</li>
</ul>

<p>Participants are expected to remain and participate for the full duration of the meeting. They must play an active role in reaching <span class="gc-dwnld-info"><abbr title="Consensus: an absence of opposition to the peer review meeting conclusions and advice.">consensus</abbr></span> on conclusions. They must commit to taking part as objective experts (for example, not advocating or actively representing an interest group, political group, client, or proponent). </p>

