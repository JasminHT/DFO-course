<p>Indicators are often used to describe what is known about a stock’s status.</p>

<aside class='bg-info'>
  <h4>Definition: </h4>
<p><strong>Indicators</strong> are measurements that provide information on the state of a stock attribute of interest. They can be assessed against thresholds that, if crossed, signal a change in <span class="gc-dwnld-info"><abbr title="Stock status: refers to a determination made, on the basis of stock assessment results, about the current condition of the stock.">stock status</abbr></span>. Common indicators include:</p> 
  <ul>
<li>  <span class="gc-dwnld-info"><abbr title="Abundance (N): total number of fish in a stock.">abundance</abbr></span></li>
<li><span class="gc-dwnld-info"><abbr title="Biomass (B): the total weight of all fish in a stock.">biomass</abbr></span></li>
<li> <span class="gc-dwnld-info"><abbr title="Spawning stock biomass (SSB): the total weight of all fish in a stock that have reached reproductive maturity.">spawning stock biomass</abbr></span></li>
</ul>
</aside>

<p>An indicator that is calculated using consistent or comparable methodology over time can be used to develop a relative index. Where data is limited, relative indices may be used in stock assessments to track trends.</p>
 
<p>Stock indicators can be influenced by: </p>
  <ul>
<li>prey availability</li>
<li>predator abundance</li>
<li>climate change and other environmental conditions</li>
</ul>

<p>Incorporating these broader ecosystem considerations into stock assessments contributes to an <span class="gc-dwnld-info"><abbr title="Ecosystem approach to fisheries management (EAFM): incorporates ecosystem variables (for example, climate, oceanographic and ecological factors) into single-species stock assessments and scientific advice to better inform decision making. This is particularly important when predator-prey interactions indicate that management decisions on 1 stock have consequences to others.">ecosystem approach to fisheries management</abbr></span>. </p>

<p>Some stock assessments may include information on ecosystems and environmental conditions to improve the interpretation of historical information and the accuracy of <span class="gc-dwnld-info"><abbr title="Projections: explore and compare the outcomes of many scenarios that could happen.">projections</abbr></span>. This can include:</p>
<ul>
  <li>oceanographic conditions </li>
  <li>hydrographic conditions</li>
  <li>availability of food or prey</li>
  <li>predation</li>
  <li>habitat</li>
  <li>climate change</li>
</ul>


<aside class='bg-warning'>
  <h4>Key concept: </h4>
  <p>Climate change poses serious risks to the future of Canada’s fisheries. The <a href="https://laws-lois.justice.gc.ca/eng/acts/F-14/page-1.html"><i>Fisheries Act</i></a> requires DFO to consider environmental conditions affecting stocks in the development and implementation of management action. Across Canada, DFO scientists are exploring the ecosystem impacts of changing ocean conditions, such as oxygen depletion and sea ice loss, by studying the response of various fish stocks to these changes.</p></aside>


