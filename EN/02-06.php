  <h3>Example: Community-based fishery-independent monitoring in the western Canadian Arctic </h3>
  <p>Subsistence fisheries in the Arctic play a critical role in Indigenous cultural identity, economic resilience, and food security. In the Northwest Territories Inuvialuit Settlement Region and Gwich’in Settlement Area, Indigenous co-management partners identified historical declines of Arctic Char and Dolly Varden and expressed a desire to monitor priority stocks. </p>

  <p>Following a standardized approach, Inuit and Gwich’in monitors collect data at traditional camps.</p>

  <p>They are responsible for determining: </p>
  <ul>
    <li>  when to start data collection</li>
    <li>  where to set nets </li>
    <li>  sampling location</li>
  </ul>

  <p>Inuit and Gwich’in monitors review and help interpret results with DFO at annual working group meetings to inform stock status. </p>

