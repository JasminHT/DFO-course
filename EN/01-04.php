    <p>The Government of Canada’s department of Fisheries and Oceans Canada (DFO) manages fisheries in Canada according to the <a href="https://laws-lois.justice.gc.ca/eng/acts/F-14/page-1.html"><i>Fisheries Act</i></a> using credible, science-based, affordable, and effective practices. DFO manages over 200 fisheries on 3 coasts (Pacific, Arctic, Atlantic). DFO is responsible for determining:</p>
    <ul>
      <li>how much is fished (for example, total allowable catch)</li>
      <li>who gets to fish (for example, permits and licensing)</li>
      <li>where fishing can occur (for example, outside of marine protected areas and other prohibited areas, temporary closures due to the presence of migratory marine mammals)</li>
      <li>when fishing occurs (for example, times and seasons)</li>
      <li>how fishing can occur (for example, gear type, size, and quantity, monitoring and reporting requirements) </li>
    </ul>


  
