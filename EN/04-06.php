<aside class='bg-info'>
  <h4>Definition: </h4>
  <p>Discussion and debate are important components of the peer review process. <strong>Consensus</strong> is an absence of opposition to the peer review meeting conclusions and advice. Consensus is reached based on scientific data and information and not on external considerations, such as potential impacts of future decisions.</p></aside>

<p>Consensus is reached by considering alternate interpretations and contrary opinions. Sharing differing opinions is encouraged because science advice will benefit from their input. In many cases, consensus can be reached when participants believe that additional data or more thorough analyses could support another conclusion, but they do not oppose the proposed conclusions. </p>

<p>It is the responsibility of the meeting chair(s) to remind participants that the conclusions and advice must be based on demonstrated evidence or facts. When it is apparent that different conclusions are supported by various sources of data or by different but valid scientific analyses, consensus should be developed by considering the weight of evidence for each proposed conclusion. </p>

<p>If different conclusions are equally supported by data and advice and are equally plausible, this information is included in a section of the advice entitled “Alternative Interpretations or Information”. Equally plausible conclusions and evidence-based alternate perspectives that are not part of the final advice can be captured in Science Advisory Reports. </p>



