    <p>DFO recognizes that the inclusion of <strong>Indigenous Ways of Knowing</strong> alongside and equal to western science: </p>
    <ul>
      <li>enriches the collective knowledge and understanding of freshwater, coastal, and marine environments</li>
      <li>improves DFO’s decision making </li>
      <li>establishes respectful relationships with Indigenous Peoples</li>
      <li>advances reconciliation to address the harms of colonization </li>
    </ul>

    <aside class='bg-info'>
    <h4>What are Indigenous Ways of Knowing?</h4>
    <p>There is no universal definition of Indigenous Ways of Knowing. Its composition is determined by First Nations, Inuit, and Métis using their own terms. Indigenous Ways of Knowing may include Indigenous:</p>
    <ul>  
      <li>knowledge systems</li>
      <li>science (for example, research and monitoring led by Indigenous Peoples)</li>
      <li>languages </li>
      <li>worldviews </li>
      <li>values</li>
    </ul>
    </aside>

    <p>Indigenous Ways of Knowing may be shared by Knowledge Holders whom the Indigenous Nation, government, community, or organization deems appropriate, including Indigenous:   </p>
    <ul>
      <li>Elders </li>
      <li>experts</li>
      <li>harvesters</li>
      <li>youth</li>
      <li>other Knowledge Keepers </li>
    </ul>

    <p>Participation and collaboration with Indigenous Peoples and the inclusion of Indigenous Ways of Knowing are encouraged at every step of the stock assessment process, from data collection to providing science advice. Which Indigenous Ways of Knowing are relevant to a particular process and how and when they are shared with DFO is determined by Indigenous Peoples. </p>

    <p>Common points of entry and inclusion of Indigenous Ways of Knowing in the stock assessment process include: </p>
    <ul>
      <li>the co-development of research questions of mutual interest </li>
      <li>collaborative research teams </li>
      <li>research and monitoring activities led by Indigenous Peoples</li>
      <li>participation of Indigenous experts in Canadian Science Advisory Secretariat (CSAS) peer review processes</li>
    </ul>


  <p>While Indigenous socio-economic information is not incorporated into scientific stock assessments, per section 2.5(d) of the <a href="https://laws-lois.justice.gc.ca/eng/acts/F-14/page-1.html#h-1175155"><i>Fisheries Act</i></a>, it may be considered in decision making alongside stock assessment outputs.</p>




  




