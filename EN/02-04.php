<h2>Unit 2.2: Data collection</h2>

<p>For high-quality stock assessments to be produced, data must be accurate and timely. However, data collection can be challenging because:</p>
<ul>
  <li>most fish cannot be directly observed underwater </li>
  <li>many species migrate to varying extents </li>
  <li>it takes time for population-level changes to be accurately reflected in the data for long-lived species</li>
</ul>

