<h2>Unit 2.2: Data collection</h2>

<p>DFO regularly carries out monitoring surveys to collect fishery-independent data. For example, DFO conducts multi-species surveys that cover much of Canada’s continental shelf. </p>

<p>Monitoring undertaken by <span class="gc-dwnld-info"><abbr title="Rights-holders: modern successors to the Indigenous collectives that signed treaties, occupied and used land, and engaged in historic practices that support a finding of an Aboriginal right or Aboriginal title.">Rights-holders</abbr></span> and stakeholders, such as Indigenous Peoples and fishing industries, beyond their harvesting activities is also a critical source of fisheries-independent knowledge, expertise, and capacity in Canada. Rights-holders and stakeholders participate in many collaborative monitoring programs that provide key data for DFO’s stock assessments. Indigenous Ways of Knowing are also holistic and can provide important context for stock assessments (for example, historic environmental conditions, historic species distribution, understanding of species’ life history).</p>  

<p>International countries also carry out monitoring surveys that inform joint stock assessments for transboundary or straddling stocks. </p>

