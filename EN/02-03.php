  <h2>Unit 2.1: Types of data</h2>



<p>Although it is a valuable source of information, fishery-dependent data alone may not accurately represent overall population dynamics and stock trends. This may lead to perceptions of <strong>hyperstability</strong> (where catch remains high as the fish stock declines) or <strong>hyperdepletion</strong> (where catch declines faster than fish abundance) because:</p>

<ul>
<li>fishing can occur selectively where fish cluster using highly efficient gear</li>
<li>different types of gear are selective for different sizes, ages, and species </li>
<li>there may be a greater economic incentive for catching certain species or sizes of fish</li>
<li>fishing site selection may be influenced by proximity to coastal communities, as well as fuel and labour costs </li>
</ul>

