<p>Le MPO doit assurer l’utilisation durable du poisson en tant que ressource renouvelable. L’évaluation des stocks vise à fournir des avis scientifiques aux gestionnaires au sujet des risques et de l’incertitude associés à la prise de différentes mesures de gestion. </p>

<p>Les évaluations des stocks utilisent des calculs statistiques et mathématiques pour déterminer l’état d’un stock et, dans la mesure du possible, projeter comment il réagira à différentes mesures de gestion dans l’avenir. Les résultats des évaluations des stocks du MPO sont présentés et validés lors des réunions d’examen scientifique par les pairs. Par la suite, un avis scientifique est produit pour guider la prise de décisions.</p>

<p>Cliquez sur ces encadrés pour vous familiariser avec d’autres  termes clés : </p>



<div class='collapsible blue' id="module1">
  <details>
    <summary><strong>Abondance</strong></summary>
    <p>L’<strong>abondance (N) </strong>est le nombre total de poissons dans un stock. </p>
  </details>
</div>

<div class='collapsible blue' id="module2">
  <details>
    <summary><strong>Biomasse</strong></summary>
    <p>La <strong>biomasse (B)</strong> est le poids total de tous les poissons d’un stock. </p>
  </details>
</div>


<div class='collapsible blue' id="module3">
  <details>
    <summary><strong>Productivité</strong></summary>
    <p>La <strong>productivité</strong> est le taux auquel la biomasse est produite dans un stock. </p>
  </details>
</div>


<div class='collapsible blue' id="module4">
  <details>
    <summary><strong>Biomasse du stock reproducteur </strong></summary>
    <p>La <strong>biomasse du stock reproducteur (BSR)</strong> est le poids total de tous les poissons d’un stock qui ont atteint la maturité de reproduction.</p>
  </details>
</div>


<div class='collapsible blue' id="module5">
  <details>
    <summary><strong>Recrutement</strong></summary>
    <p>Le <strong>recrutement</strong> a lieu lorsque de nouveaux poissons sont ajoutés à une population. Il est mesuré lorsque des poissons petits ou jeunes sont assez grands ou assez vieux pour être capturés dans une pêcherie (qui peut être affectée par les limites de capture ou le type d'engin) ou dans une étude scientifique. </p>
  </details>
</div>


<div class='collapsible blue' id="module6">
  <details>
    <summary><strong>Cohorte </strong></summary>
    <p>Une <strong>cohorte</strong> ou classe d’âge est constituée de tous les poissons d’un stock nés au cours de la même année de fraie. La quantité du recrutement détermine la force (c.-à-d. l’abondance relative) d’une cohorte. </p>
  </details>
</div>




<div class='collapsible blue' id="key2">
  <details>
    <summary><strong>Mortalité par la pêche</strong></summary>
    <p>La <strong>mortalité par la pêche</strong> est la mesure des mortalités dans un stock attribuables à toutes les activités de pêche, y compris :</p>
  <ul>
    <li>la pêche dirigée&nbsp;;</li>
    <li>les <span class="gc-dwnld-info"><abbr title="Prises accessoires : Prises accidentelles d’une espèce dans une pêche qui en cible une autre. La partie conservée des prises accessoires est appelée prises fortuites, et la partie rejetée est appelée rejets.">prises accessoires</abbr></span> et les remises&nbsp;;</li>
    <li>les autres mortalités fortuites dans d’autres pêches.</li>
  </ul> 
  </details>
</div>

<div class='collapsible blue' id="key3">
  <details>
    <summary><strong>Mortalité naturelle</strong></summary>
    <p>La <strong>mortalité naturelle</strong> est la mesure des mortalités dans un stock dues à toutes les causes autres que la pêche, comme : </p>
    <ul>
    <li>la prédation&nbsp;;</li>
    <li>la maladie&nbsp;;</li>
    <li>la disponibilité des proies&nbsp;;</li>
    <li>la concurrence pour les ressources.</li>
      </ul>
     
  </details>
</div>

<div class='collapsible blue' id="key4">
  <details>
    <summary><strong>Effort de pêche</strong></summary>
    <p>L’<strong>effort de pêche</strong> est une mesure de l’activité de pêche utilisant des paramètres comme :</p> 
    <ul>
      <li>le nombre de filets ou de lignes utilisés&nbsp;;</li>
      <li>le nombre de bateaux de pêche déployés&nbsp;;</li>
      <li>le nombre d’heures de pêche.</li>
    </ul>
  </details>
</div>

<div class='collapsible blue' id="key5">
  <details>
    <summary><strong>Capturabilité </strong></summary>
    <p>La <strong>capturabilité</strong> est un concept qui reflète l'efficacité d'une pêcherie. Il s'agit de la combinaison de la <strong>disponibilité</strong> (la probabilité qu'un poisson entre dans l'engin de pêche) et de la <strong>sélectivité</strong> (la probabilité de rétention une fois qu'un poisson entre dans l'engin de pêche). La sélectivité de l'engin est influencée par sa conception (type et taille des mailles, panneaux d'échappement, taille des hameçons).</p> 
  </details>
</div>




