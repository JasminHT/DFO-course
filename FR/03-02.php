<h2>Unité 3.1 : Produits</h2>

<p>Les évaluations des stocks fondées sur des modèles peuvent souvent servir à projeter comment les stocks pourraient réagir à différentes mesures de gestion. Les projections sont habituellement à court terme (p. ex. 2 à 4 ans), mais elles peuvent aussi être à long terme (p. ex. 20 à 40 ans). Plus les projections regardent loin dans l’avenir, plus l’incertitude est grande. Les projections à long terme sont plus utiles pour évaluer les stratégies de gestion globales que pour prendre des décisions à court terme.</p>


