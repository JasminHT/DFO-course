<h2>Unité 1.1 : Approche canadienne des sciences halieutiques</h2>



<p>À l’heure actuelle, le Canada adopte une approche monospécifique d’évaluation des stocks. Cette approche examine l’impact de la pêche sur une espèce à la fois. Les résultats guident uniquement les décisions de gestion concernant cette pêche.</p>

<aside class='bg-info'>
  <h4>Définition : </h4>
  <p>Une <strong>approche écosystémiques de la gestion des pêches</strong> intègre des variables écosystémiques (par exemple, des facteurs climatiques, océanographiques et écologiques) dans les évaluations des stocks d'une seule espèce et dans les avis scientifiques afin de mieux éclairer la prise de décision. Cela est particulièrement important lorsque les interactions prédateur-proie indiquent que les décisions de gestion concernant un stock ont des conséquences sur d'autres stocks. </p>
</aside>

