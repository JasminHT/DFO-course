<h2>Unité 1.1 : Approche canadienne des sciences halieutiques</h2>



<p>À l’heure actuelle, le Canada adopte une approche monospécifique d’évaluation des stocks. Cette approche examine l’impact de la pêche sur une espèce à la fois. Les résultats guident uniquement les décisions de gestion concernant cette pêche.</p>

<aside class='bg-info'>
  <p>Une <strong>approche écosystémiques de la gestion des pêches</strong> intègre les considérations environnementales dans l’évaluation des stocks. Elle mène à une prise de décisions plus éclairée parce qu’elle inclut l’impact non seulement de la pêche, mais aussi des facteurs écosystémiques et environnementaux. </p>
</aside>

