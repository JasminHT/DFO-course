<p>Le MPO tend actuellement vers l’adoption d’une approche écosystémique de la gestion des pêches qui permettra :</p>

<ul>
  <li>de favoriser une industrie de la pêche durable, économiquement viable et concurrentielle sur la scène internationale&nbsp;;</li>
  <li>d’améliorer la gestion des pêches canadiennes en permettant la prise de décisions plus judicieuses&nbsp;;</li>
  <li>de suivre la transition mondiale vers des approches écosystémique de la gestion des pêches&nbsp;;</li>
  <li>d’aider à faire face aux défis des changements climatiques et d’autres changements importants au niveau de l’écosystème sur les stocks de poissons&nbsp;;</li>
  <li>d’aider à répondre aux obligations stratégiques, aux engagements internationaux, aux exigences en matière d’écocertification et aux exigences du commerce international relatives à des pêches durables&nbsp;;</li>
  <li>de répondre à la demande des consommateurs pour des pêches durables.</li>

</ul>


