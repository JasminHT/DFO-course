  <h2>Unité 2.1 : Types de données</h2>



<p>Bien qu’il s’agisse d’une source d’information précieuse, les données dépendantes de la pêche à elles seules peuvent ne pas représenter exactement la dynamique globale des populations et les tendances du stock. Cela peut mener à des perceptions d’<strong>hyperstabilité</strong> (lorsque les prises demeurent élevées alors que le stock de poissons diminue) d’<strong>hyperappauvrissement</strong> (lorsque les prises diminuent plus rapidement que l’abondance du poisson) parce que :</p>

<ul>
<li>la pêche peut être menée sélectivement aux endroits où les poissons se regroupent à l’aide d’engins très efficaces&nbsp;;</li>
<li>différents types d’engins sont sélectifs pour des tailles, des âges et des espèces différents&nbsp;;</li>
<li>la pêche de certaines espèces ou certaines tailles de poissons peut être économiquement plus intéressante&nbsp;;</li>
<li>le choix des lieux de pêche peut être influencé par la proximité des collectivités côtières, ainsi que par les coûts du carburant et de la main-d’œuvre.</li>

</ul>

