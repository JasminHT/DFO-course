  <h3>Exemple : saumon rouge</h3>
  <p>Les stocks de saumon du Pacifique sont souvent composés de plusieurs populations qui se reproduisent dans différentes rivières. Une <b>unité de conservation (UC)</b> est un groupe de saumons sauvages qui est isolé des autres groupes. Si une UC disparaît localement, il est peu probable qu’elle recolonise cette zone. Une UC peut être composée d’une ou de plusieurs populations. Les <b>zones de gestion des stocks (ZGS)</b> sont des ensembles de stocks qui sont gérés conjointement avec un état du stock commun. Il y a 216 UCs de saumon rouge qui constituent 16 ZGS.</p>

