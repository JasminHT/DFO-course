<h2>Unité 1.3 : État des stocks et points de référence</h2>

<figure>
  <img src="../images/manila_clam-palourde_japonaise.jpg">
  <figcaption> Palourdes japonaises sur une plage. <br>Photo credit: DFO</figcaption>
