<h2>Unité 4.2 : Participer aux réunions d’examen par les pairs</h2>

<figure>
  <img src="../images/table-meeting.jpg">
  <figcaption>Source de la photo : Shutterstock
</figcaption>
</figure>

<p>La participation inclusive aux réunions d’examen par les pairs donne lieu à une discussion complète et ouverte. Elle apporte des points de vue divergents à la table et tire des conclusions scientifiques qui, autrement, ne seraient peut-être pas prises en compte. La participation aux réunions d’examen par les pairs du SCAS se fait uniquement sur invitation non transférable pour les experts.</p>

<p>Les experts peuvent inclure des participants :</p>
<ul>
<li>du MPO ;</li>
<li>d’autres ministères fédéraux, provinciaux ou territoriaux ;</li>
<li>des nations, communautés ou organisations autochtones ;</li>
<li>de l’industrie ;</li>
<li>du monde universitaire ;</li>
<li>d’organisations non gouvernementales ;</li>
<li>d’organisations internationales.</li>
</ul>


<p>Les comités directeurs doivent veiller à ce que la taille des listes de participants soit gérable en fonction du format de la réunion pour permettre une discussion approfondie. La liste finale des participants doit refléter :</p>
<ul>
<li>la nature du problème ;</li>
<li>l’expertise requise ;</li>
<li>la diversité des opinions scientifiques sur la question ;</li>
<li>les détenteurs de connaissances locales et autochtones, le cas échéant.</li>

</ul>

<p>Les participants doivent posséder l’expertise requise pour :</p>
<ul>
<li>poser des questions de façon constructive ;</li>
<li>commenter et remettre en question l’information et les interprétations scientifiques présentées.</li>

</ul>

<p>Cette expertise peut comprendre :</p>
<ul>
<li>des connaissances scientifiques approfondies, avérées et faisant autorité, ou une formation et une expérience de travail dans le domaine ;</li>
<li>des connaissances locales et des Modes de connaissance autochtones.</li>

</ul>

<p>Les participants doivent rester à la réunion et y participer pendant toute sa durée. Ils doivent jouer un rôle actif pour parvenir à un <span class="gc-dwnld-info"><abbr title="Consensus : une absence d’opposition sur les conclusions et avis.">consensus</abbr></span> sur les conclusions. Ils doivent s’engager à participer en tant qu’experts objectifs (c.-à-d. ne pas défendre ou représenter activement un groupe d’intérêt, un groupe politique, un client ou un promoteur).</p>

