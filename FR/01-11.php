<h2>Unité 1.3 : État des stocks et points de référence</h2>


  <p>La <a href="https://www.dfo-mpo.gc.ca/reports-rapports/regs/sff-cpd/precaution-fra.htm">Politique de l’approche de précaution</a> définit 3 zones d’état des stocks pour catégoriser la santé des stocks :
  </p>
  <ul>
    <li>la zone saine&nbsp;;</li>
    <li>la zone de prudence&nbsp;;</li>
    <li>la zone critique.</li>

  </ul>

    <p>Ces zones sont créées en définissant des <strong>points de référence</strong> qui servent à délimiter les zones. L’état des stocks peut être utilisé pour :</p>
    <ul>
      <li>supporter le calcul du total autorisé des captures&nbsp;;</li>
      <li>guider les règles de décision sur les prises&nbsp;;</li>
      <li>évaluer l’efficacité des stratégies de prélèvement.</li>
 
    </ul>

    <p>Les points de référence sont utilisés comme des seuils à atteindre ou à éviter. Il existe de nombreuses méthodes différentes pour estimer des points de référence, qui dépendent de :</p>
    <ul>
      <li>la disponibilité des données&nbsp;;</li>
      <li>les connaissances biologiques sur l’espèce&nbsp;;</li>
      <li>la disponibilité du <span class="gc-dwnld-info"><abbr title="Les modèles sont des outils utilisés pour simplifier un système naturel afin qu'il soit plus facile à comprendre.">modèle</abbr></span>.</li>

    </ul>

    <p>Les points de référence peuvent être mis à jour :</p>
    <ul>
      <li>lorsque de nouvelles données deviennent disponibles&nbsp;;</li>
      <li>lorsque de nouveaux <span class="gc-dwnld-info"><abbr title="Les modèles sont des outils utilisés pour simplifier un système naturel afin qu'il soit plus facile à comprendre.">modèles</abbr></span> sont élaborés&nbsp;;</li>
      <li>lorsque la productivité du stock change.</li>

    </ul>

    <h3>Définitions des points de référence</h3>

    <div class='collapsible blue' id="module1">
      <details>
        <summary><strong>Point de référence limite</strong></summary>
        <p>Le <strong>point de référence limite (PRL)</strong> est l’état d’un stock au-dessous duquel ce dernier risque de subir de graves dommages. Il peut aussi y avoir des impacts sur l’écosystème et d’autres espèces, ainsi qu’une perte à long terme de possibilités de pêche. </p>

         <aside class='bg-info'>
          <h4>Définition : </h4>
          Un stock de poissons subissant des <strong>dommages graves</strong> peut être épuisé au point de voir sa productivité ou sa capacité de reproduction diminuée. Par conséquent, le stock serait moins résistant à la pêche et ne pourrait pas se reconstituer facilement. Les dommages graves peuvent être irréversibles ou n’être que lentement réversibles à long terme et peuvent être dus à la pêche, à d’autres impacts anthropiques ou à des causes naturelles.</aside>
      </details>
    </div>

        <div class='collapsible blue' id="module2">
      <details>
        <summary><strong>Point de référence supérieur du stock</strong></summary>
        <p>Le <strong>point de référence supérieur du stock (PRS)</strong> est un seuil au-dessous duquel il faut réduire la pêche afin d’éviter d’atteindre le PRL. Il faut donc fixer le PRS à un niveau assez élevé par rapport au PRL afin de laisser aux gestionnaires une marge de manœuvre suffisante pour détecter un déclin du stock et leur donner assez de temps pour mettre en œuvre des mesures de gestion efficaces. </p>
      </details>
    </div>

        <div class='collapsible blue' id="module3">
      <details>
        <summary><strong>Point de référence cible </strong></summary>
        <p>Le <strong>point de référence cible (PRC)</strong> est la cible générale d’un stock. Il est déterminé en fixant les objectifs de <span class="gc-dwnld-info"><abbr title="Productivité: Le taux aux auquel la biomasse est produite dans un stock.">productivité</abbr></span> du stock, les considérations écologiques d’ordre général et les objectifs socio-économiques de la pêche. Il est généralement établi au PRS ou au-dessus.</p>
      </details>
    </div>



        <div class='collapsible blue' id="module4">
      <details>
        <summary><strong>Taux d’exploitation de référence </strong></summary>
        <p>Le <strong>taux d’exploitation de référence</strong> est le taux d’exploitation maximal d’un stock de poissons. Il est inférieur ou égal au <span class="gc-dwnld-info"><abbr title="Rendement maximal durable (RMD) : La prise annuelle moyenne maximale qui peut être prélevée d’un stock sur une période indéfinie dans les conditions environnementales actuelles.">rendement maximal durable</abbr></span> du stock. Il peut être exprimé en fonction de ce qui suit :</p>
        <ul>
        <li>  <span class="gc-dwnld-info"><abbr title="Mortalité par pêche : Mesure des mortalités dans un stock attribuables à toutes les activités de pêche, y compris la pêche dirigée, les prises accessoires et les autres mortalités fortuites dans d’autres pêches.">mortalité par la pêche</abbr></span>&nbsp;;</li>
        <li><span class="gc-dwnld-info"><abbr title="Taux d’exploitation : Proportion de la biomasse recrutée ou vulnérable qui est capturée au cours d’une certaine période, généralement une année de pêche.">taux d’exploitation</abbr></span>&nbsp;;</li>
        <li>  <span class="gc-dwnld-info"><abbr title="Effort de pêche : une mesure de l’activité de pêche utilisant des paramètres comme : le nombre de filets ou de lignes utilisés ; le nombre de bateaux de pêche déployés ; le nombre d’heures de pêche.">effort de pêche</abbr></span>.</li>
        </ul>
        
      </details>
    </div>

