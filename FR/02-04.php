<p>Les évaluations des stocks peuvent nécessiter de grandes quantités de données, recueillies sur plusieurs années. La quantité de données recueillies pour un stock dépend de divers facteurs, comme l’accessibilité de la zone ou l’existence d’une méthode de collecte. Les espèces peuvent avoir une vaste répartition et un effort d’échantillonnage important peut s’avérer nécessaire pour recueillir des données sur l’ensemble de leur aire de répartition. </p>

<p>Parfois, seule une quantité limitée de données peut être recueillie pour un stock. Des évaluations des stocks peuvent être effectuées à partir de données limitées, mais elles peuvent reposer sur d’autres méthodes, comme la formulation d’un plus grand nombre  <span class="gc-dwnld-info"><abbr title="Hypothèse : Énoncé introduit dans un argument et (temporairement) accepté comme étant vrai.">d’hypothèses</abbr></span> à l’aide de renseignements sur des espèces étroitement apparentées. L’incertitude peut également être atténuée par un jugement d’expert.  
</p>










