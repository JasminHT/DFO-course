<h2>Unité 4.1 : Le Secrétariat canadien des avis scientifiques</h2>

<figure class='image-transcript'>
  <img src="../images/CSAS_EnsuresThatDFO-FR.jpg?v=1" alt='Quatre caractéristiques principales décrivent les avis scientifiques du MPO : crédibles, objectifs, inclusifs et transparents.'>
  <figcaption>
    <details id="inline-captions">
      <summary>Description</summary>      
      <p>Le Secrétariat canadien des avis scientifiques (SCAS) veille à ce que les avis scientifiques du MPO soient :</p>
      <ul>
<li><b>uniformes </b>: application d’une norme élevée d’évaluation technique à toutes les données et analyses scientifiques ;</li>
<li><b>objectifs </b>: réduction du biais dans l’interprétation des résultats ;</li>
<li><b>inclusifs </b>: mobilisation d’un groupe diversifié d’experts de divers ministères et organismes internationaux et nationaux ;</li>
<li><b>transparents </b>: publication d’un calendrier des réunions du SCAS, des avis scientifiques et de leurs documents d’appui.</li>
</ul>

      </ul>
    </details>
  </figcaption>
</figure>



<p>Les <b>principes directeurs du SCAS</b> donnent des orientations pour que les décisions du gouvernement reposent sur des avis scientifiques rigoureux. Ils sont le fondement du processus d’examen par les pairs du MPO: </p>
<ol>
<li>produire des avis opportuns et adaptés aux besoins des clients&nbsp;;</li>
<li>appliquer les méthodes scientifiques les plus pertinentes et crédibles&nbsp;;</li>
<li>offrir un accès aux documents publics&nbsp;;</li>
<li>tenir compte de diverses expertises et de différents points de vue et faire participer des experts scientifiques du MPO et externes au processus d’examen.</li>

</ol>

<p>Les processus d’examen par les pairs du SCAS peuvent être :</p>
<ul>
<li>régionaux, pour traiter les problèmes qui touchent une seule région du MPO&nbsp;;</li>
<li>zonaux, pour traiter les problèmes qui touchent plus d’une région du MPO&nbsp;;</li>
<li>nationaux, pour traiter les problèmes qui touchent l’ensemble du Canada.</li>

</ul>

