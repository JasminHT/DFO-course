<ul class='summary lst-spcd'>

  <li>La gestion des pêches vise à promouvoir la pêche durable des <span class="gc-dwnld-info"><abbr title="Stock : Groupe de poissons de la même espèce ayant un ou plusieurs attributs communs qui sont d’intérêts pour les gestionnaires.">stocks</abbr></span> de poissons et à maintenir des écosystèmes sains. Elle s’appuie sur des avis scientifiques rigoureux et sur <span class="gc-dwnld-info"><abbr title="Approche de précaution : Faire preuve de prudence lorsque les données scientifiques sont incertaines, peu fiables ou insuffisantes. Le manque ou l’absence d’informations scientifiques n’est pas un prétexte pour retarder ou empêcher la prise de mesures adéquates afin d’éviter de graves dommages à une ressource.">l’approche de précaution</abbr></span>.  </li>

  <li>Au Canada, les <span class="gc-dwnld-info"><abbr title="Pêche : Somme de toutes les activités de pêche sur une ressource donnée.">pêches</abbr></span> sont gérées par le Ministère des Pêches et des Océans Canada (MPO) dans l’intérêt de tous les Canadiens. Le MPO étant un ministère à vocation scientifique, l’intégrité scientifique est essentielle à son travail et à celui de ses employés.</li>

  <li>Les <span class="gc-dwnld-info"><abbr title="Détenteurs de droits: les successeurs modernes des collectivités autochtones qui ont signé des traités, qui ont occupé et utilisé les terres et se sont livrés à des pratiques historiques qui permettent de conclure à l’existence d’un droit ou d’un titre ancestral.">détenteurs de droits autochtones</abbr></span> et les intervenants contribuent beaucoup aux sciences halieutiques au Canada. </li>

  <li>Dans la mesure du possible, les évaluations des stocks permettent de projeter la réaction des stocks de poissons à différentes mesures de gestion. Les scientifiques peuvent conseiller les gestionnaires sur les risques et l’incertitude associés à l’application de différentes mesures de gestion.</li>

  <li>Les évaluations des stocks estiment l’état d’un stock. On crée les zones d’état saine, de prudence et critique en définissant des points de référence qui servent de seuils entre elles. 

    <ul>
    <li>Le point de référence limite (PRL) est l’état d’un stock au-dessous duquel ce dernier risque de subir de <span class="gc-dwnld-info"><abbr title="Dommages grave : Un stock de poissons subissant des dommages graves peut être épuisé au point de voir sa productivité ou sa capacité de reproduction diminuée. Par conséquent, le stock serait moins résistant à la pêche et ne pourrait pas se reconstituer facilement. Les dommages graves peuvent être irréversibles ou n’être que lentement réversibles à long terme et peuvent être dus à la pêche, à d’autres impacts anthropiques ou à des causes naturelles.">graves dommages</abbr></span>. </li>

    <li>Le point de référence supérieur du stock (PRS) est le seuil au-dessous duquel il faut réduire la pêche afin d’éviter d’atteindre le PRL. </li>

    <li>Le taux d’exploitation de référence est le taux d’exploitation maximal d’un stock de poissons. </li>

    <li>Le point de référence cible (PRC) est la cible générale d’un stock.</li>
  </ul>

</li>



  <li>Une approche courante utilisée pour définir les points de référence consiste à les estimer sur la base de le rendement maximal durable d'un stock. Les indicateurs sont souvent utilisés pour décrire ce que l'on sait sur l'état d'un stock.</li>

</ul>

