<aside class='bg-info'>
<p>Les <strong>indicateurs</strong> sont des mesures qui renseignent sur l’état d’une composante d’intérêt du stock. Ils peuvent être évalués en fonction de seuils qui, s’ils sont dépassés, signalent un changement dans l’état du stock. Indicateurs courants :</p> 
  <ul>

<li><span class="gc-dwnld-info"><abbr title="Abondance (N) : Nombre total de poissons dans un stock.">l’abondance</abbr></span>&nbsp;;</li>

<li><span class="gc-dwnld-info"><abbr title="iomasse (B) : Le poids total de tous les poissons d’un stock.">la biomasse</abbr></span>&nbsp;;</li>
<li><span class="gc-dwnld-info"><abbr title="Biomasse du stock reproducteur (BSR) : Poids total de tous les poissons d’un stock qui ont atteint la maturité de reproduction.">la biomasse du stock reproducteur</abbr></span>.</li>
</ul>
</aside>

<p>Un indicateur calculé à l’aide d’une méthodologie cohérente ou comparable dans le temps peut être utilisé pour développer un indice relatif. Lorsque les données sont limitées, les indices relatifs peuvent être utilisés dans les évaluations des stocks pour suivre les tendances.</p>
 
<p>Les indicateurs d’un stock peuvent être influencés par différents facteurs : </p>
  <ul>
  <li>disponibilité des proies&nbsp;;</li>
  <li>abondance des prédateurs&nbsp;;</li>
  <li>changements climatiques et autres conditions environnementales.</li>

</ul>

<p>L’intégration de ces considérations écosystémiques plus générales dans les évaluations des stocks mène à une <span class="gc-dwnld-info"><abbr title="Approche écosystémique de la gestion des pêches (AEP) : incorpore des considérations environnementales dans l’évaluation des stocks. Cela permet de prendre des décisions plus éclairées, car l’impact non seulement de la pêche, mais aussi de l’écosystème et des facteurs environnementaux est pris en compte.">approche écosystémique de la gestion des pêches</abbr></span>. </p>

<p>Certaines évaluations des stocks peuvent comprendre des renseignements sur les écosystèmes et les conditions environnementales afin d’améliorer l’interprétation des données historiques et la précision des projections, notamment :</p>
<ul>
  <li>conditions océanographiques&nbsp;;</li>
  <li>conditions hydrographiques&nbsp;;</li>
  <li>disponibilité de la nourriture ou des proies&nbsp;;</li>
  <li>prédation&nbsp;;</li>
  <li>habitat&nbsp;;</li>
  <li>changements climatiques.</li>

</ul>


<aside class='bg-warning'>Les changements climatiques posent de graves risques pour l’avenir des pêches au Canada. La <a href="https://laws-lois.justice.gc.ca/fra/lois/f-14/page-1.html"><i>Loi sur les pêches</i></a> exige que le MPO tienne compte des conditions environnementales qui influencent les stocks pour élaborer et mettre en œuvre des mesures de gestion. Partout au Canada, les scientifiques du MPO explorent les impacts sur les écosystèmes des conditions changeantes des océans, comme l’appauvrissement en oxygène et la perte de glace de mer, en étudiant la réaction de divers stocks de poissons à ces changements.</aside>


