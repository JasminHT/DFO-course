  <h2>Glossaire</h2>
  <p>Voici les définitions de certains termes techniques que l’on trouve au cours d’Introduction à l’évaluation des stocks.</p>
  <p></p>

<div id='glossary'>

  <dl class='lst-spcd'>
<dt>Abondance (N) : </dt><dd>Nombre total de poissons dans un stock.</dd>
<dt>Appauvrissement : </dt><dd>Mesure de la biomasse actuelle d’un stock par rapport à la biomasse moyenne non pêchée.</dd>
<dt>Approche de précaution : </dt><dd>Faire preuve de prudence lorsque les données scientifiques sont incertaines, peu fiables ou insuffisantes. Le manque ou l’absence d’informations scientifiques n’est pas un prétexte pour retarder ou empêcher la prise de mesures adéquates afin d’éviter de graves dommages à une ressource.</dd>
<dt>Approche écosystémique de la gestion des pêches (AEP) : </dt><dd>L’AEP intègre des variables écosystémiques (par exemple, des facteurs climatiques, océanographiques et écologiques) dans les évaluations des stocks d'une seule espèce et dans les avis scientifiques afin de mieux éclairer la prise de décision. Cela est particulièrement important lorsque les interactions prédateur-proie indiquent que les décisions de gestion concernant un stock ont des conséquences sur d'autres stocks.</dd>
<dt>Analyse de sensibilité : </dt><dd>Permettre aux scientifiques d’explorer les effets de l’incertitude et des hypothèses en testant différents scénarios (comme une variation aléatoire dans le recrutement ou différents facteurs environnementaux ayant une incidence sur la mortalité naturelle).</dd>
<dt>Biomasse (B) : </dt><dd>Le poids total de tous les poissons d’un stock.</dd>
<dt>Biomasse du stock reproducteur (BSR) : </dt><dd>Poids total de tous les poissons d’un stock qui ont atteint la maturité de reproduction.</dd>
<dt>Biomasse non exploitée : </dt><dd>Poids total des poissons d’un stock lorsqu’il n’est pas exploité.</dd>
<dt>Capacité de charge (K) : </dt><dd>La taille du stock est limitée par des facteurs environnementaux et la disponibilité des ressources.</dd>
<dt>Capture par unité d’effort : </dt><dd>Quantité de poissons capturés avec une unité standard d’effort de pêche (par exemple, le nombre de poissons capturés pour 1000 hameçons par jour ou le poids des poissons capturés par heure de chalutage). La CPUE est souvent considérée comme un indice d’abondance relative (c’est-à-dire qu’elle est proportionnelle à la part de la biomasse (ou des effectifs) du stock qui est vulnérable à l’engin de pêche).</dd>
<dt>Capturabilité : </dt>
<dd>Un concept qui reflète l'efficacité d'une pêcherie. Il s'agit de la combinaison de la disponibilité (la probabilité qu'un poisson entre dans l'engin de pêche) et de la sélectivité (la probabilité de rétention une fois qu'un poisson entre dans l'engin de pêche). La sélectivité de l'engin est influencée par sa conception (type et taille des mailles, panneaux d'échappement, taille des hameçons).</dd>
<dt>Casier : </dt><dd> Engins de pêche statiques qui fonctionnent passivement pour attraper ou piéger les poissons qui s’en approchent ou qui y pénètrent.</dd>

<dt>Chalutage : </dt><dd>Méthode de pêche qui consiste à tirer derrière un ou plusieurs bateaux un filet de pêche lourdement lesté pour le maintenir sur le fond marin dans l’eau. Le filet utilisé pour le chalutage est appelé chalut.</dd>
<dt>Cohorte ou classe d’âge : </dt><dd>Tous les poissons d’un stock nés au cours de la même année de fraie.</dd>
<dt>Conflit d’interets :</dt><dd> Un intérêt du participant susceptible d’influer, ou d’être perçu comme influant, sur son objectivité et son indépendance lorsqu’il participe au processus d’examen par les pairs.</dd>
<dt>Consensus : </dt><dd>L’absence d’opposition aux conclusions et aux avis de la réunion d’examen par les pairs.</dd>

<dt>Détenteurs de droits autochtones : </dt><dd>Les successeurs modernes des collectivités autochtones qui ont signé des traités, qui ont occupé et utilisé les terres et se sont livrés à des pratiques historiques qui permettent de conclure à l’existence d’un droit ou d’un titre ancestral. Les droits autochtones sont les droits collectifs des peuples Autochtones découlant de leur statut de premiers peuples du Canada et peuvent varier d’une nation à l’autre et d’une communauté à l’autre. Ils renvoient aux pratiques, traditions et coutumes qui font partie intégrante de la culture distinctive d’un groupe autochtone. Ces droits sont propres à un site, une communauté ou un fait, et reposent sur des faits historiques. Les droits issus de traités sont des droits autochtones qui sont soit établis ou codifiés dans des traités entre la Couronne et les Autochtones. Le titre ancestral, aussi appelé « titre aborigène », est un type de droit autochtone. Il se rapporte au droit d’avoir le contrôle sur l’utilisation et l’occupation d’un territoire.</dd>
<dt>Dommages grave : </dt><dd>Un stock de poissons subissant des dommages graves peut être épuisé au point de voir sa productivité ou sa capacité de reproduction diminuée. Par conséquent, le stock serait moins résistant à la pêche et ne pourrait pas se reconstituer facilement. Les dommages graves peuvent être irréversibles ou n’être que lentement réversibles à long terme et peuvent être dus à la pêche, à d’autres impacts anthropiques ou à des causes naturelles.</dd>
<dt>Données dépendantes de la pêche :</dt><dd> Recueillies directement auprès des pêches commerciales, récréatives ou autochtones à des fins alimentaires, sociales et rituelles (ASR).</dd>
<dt>Données indépendantes de la pêche :</dt><dd> Recueillies en dehors des pêches.</dd>
<dt>Disponibilité : </dt>
<dd>La probabilité qu'un poisson entre dans l'engin de pêche.</dd>
<dt>Dragage : </dt><dd>Méthode de pêche dans laquelle une drague est traînée sur le fond marin, en le grattant ou en y pénétrant.</dd>
<dt>État du stock : </dt><dd>Une décision prise, sur la base des résultats de l’évaluation du stock, au sujet de l’état actuel du stock.</dd>
<dt>Effort de pêche : </dt><dd>Une mesure de l’activité de pêche utilisant des paramètres comme&nbsp;:

<ul>
  <li>le nombre de filets ou de lignes utilisés&nbsp;;</li>
  <li>le nombre de bateaux de pêche déployés&nbsp;;</li>
  <li>le nombre d’heures de pêche.</li>
</ul>

</dd>
<dt>Évaluation du stock : </dt><dd>Le processus scientifique d’analyse des données pour estimer l’abondance et la productivité passées, présentes et futures des stocks de poissons.</dd>
<dt>Filet maillant : </dt><dd>Filet de pêche qui pend verticalement de façon à ce que les poissons y restent piégés par leurs branchies.</dd>
<dt>Hydroacoustique : </dt><dd>Étude du son dans l’eau. </dd>
<dt>Hyperappauvrissement : </dt><dd>Les prises diminuent plus rapidement que l’abondance du poisson.</dd>
<dt>Hyperstabilité : </dt><dd>Les prises demeurent élevées alors que le stock de poissons diminue.</dd>

<dt>Hypothèse : </dt><dd>Énoncé introduit dans un argument et (temporairement) accepté comme étant vrai. </dd>
<dt>Incertitude : </dt><dd>L’incomplétude des connaissances concernant un système naturel.</dd> <dt>Indicateurs : </dt><dd>Mesures qui renseignent sur l’état d’une composante d’intérêt du stock. Une série d’indicateurs dans le temps crée un indice. </dd>
<dt>Intervenants : </dt><dd>Pour cette formation, personnes ou organisations qui s’intéressent aux sciences halieutiques et à la gestion des pêches et du milieu marin.</dd>
<dt>Modèles : </dt><dd>Des outils utilisés pour simplifier un système naturel afin qu'il soit plus facile à comprendre. </dd>
<dt>Modèles opérationnels : </dt><dd>Des modèles informatiques qui simulent la croissance, la reproduction et l’interaction des stocks de poissons avec l’environnement. </dd>
<dt>Modes de connaissance autochtones : </dt><dd>Il n’existe pas de définition universelle des Modes de connaissance autochtones. Leur composition est déterminée par les Premières Nations, les Inuits et les Métis selon leurs propres termes. Les Modes de connaissance autochtones peut inclure les systèmes du savoir autochtones, science (p. ex. la recherche et la surveillance menées par les peuples Autochtones), langues, visions du monde, et valeurs autochtones. </dd>


<dt>Moratoire&nbsp;: </dt><dd>Interdiction temporaire d’une activité.</dd>
<dt>Mortalité par la pêche&nbsp;: </dt><dd>La mesure des mortalités dans un stock attribuables à toutes les activités de pêche, y compris&nbsp;:

<ul>
  <li>la pêche dirigée&nbsp;;</li>
  <li>les prises accessoires et les remises&nbsp;;</li>
  <li>les autres mortalités fortuites dans d’autres pêches.</li>

</ul>
</dd>

<dt>Mortalité naturelle : </dt><dd>

La mesure des mortalités dans un stock dues à toutes les causes autres que la pêche, comme :

<ul>
  <li>la prédation ;</li>
  <li>la maladie ;</li>
  <li>la disponibilité des proies ;</li>
  <li>la concurrence pour les ressources.</li>
</ul>

</dd>
<dt>Otolithe : </dt><dd>Les petites structures dans l’oreille interne avec des anneaux de croissance qui peuvent être comptés.</dd>
<dt>Palangre : </dt><dd>Type d’engin de pêche constitué d’une longue ligne maîtresse à laquelle sont pendues à intervalles réguliers des lignes plus courtes munies d’hameçons appâtés.</dd>
<dt>Paramètre : </dt><dd>Les nombres qui résument une caractéristique mesurable d’un stock.</dd>

<dt>Pêche : </dt><dd>Somme de toutes les activités de pêche sur une ressource donnée.</dd>
<dt>Pêche à l’électricité : </dt><dd>Champ électrique émis par un générateur d’impulsions pour étourdir temporairement les poissons que l’on peut ensuite recueillir au moyen d’une épuisette.</dd>
<dt>Pêche autochtone à des fins alimentaires, sociales et rituelles (ASR) : </dt><dd>Le droit de pêcher à des fins alimentaires, sociales et rituelles est protégé en vertu de l’article 35 de la Constitution. Il s’agit d’un droit collectif, et non individuel. Les pêcheurs Autochtones désignés sont autorisés à capturer ce qui est nécessaire pour eux-mêmes ou leur communauté à des fins alimentaires, sociales et rituelles (ASR). La pêche à des fins ASR ne permet pas de vendre les prises. Elle peut être pratiquée à divers moments de l’année, qui ne correspondent pas toujours aux saisons ou aux zones de la pêche commerciale. Les décisions concernant l’ouverture ou la fermeture d’une pêche sont prises dans le cadre du processus de gestion du poisson.</dd>
<dt>Pélagique : </dt><dd>Poisson habitant la colonne d’eau de la pleine mer entre la surface et le fond marin. </dd>
<dt>Point de référence : </dt><dd>Un repère par rapport auquel la biomasse ou l’abondance du stock ou le taux de mortalité par pêche (ou taux d’exploitation) peuvent être mesurés afin de déterminer son état. Ces points de référence peuvent être des objectifs, des seuils ou des limites, selon l’usage auquel ils sont destinés.</dd>
<dt>Point de référence cible (PRC) : </dt><dd>La cible générale d’un stock.</dd>
<dt>Point de référence limite (PRL) : </dt><dd>L’état d’un stock au-dessous duquel ce dernier risque de subir de graves dommages.</dd>
<dt>Point de référence supérieur du stock (PRS) : </dt><dd>Un seuil au-dessous duquel il faut réduire la pêche afin d’éviter d’atteindre le PRL.</dd>
<dt>Poisson : </dt><dd>Dans la <i>Loi sur les pêches</i>, le terme « poisson » inclut:
<ul>
  <li>les poissons, les mollusques, les crustacés et les animaux marins&nbsp;;</li>
  <li>toute partie des poissons, des mollusques, des crustacés et des animaux marins&nbsp;;</li>
  <li>les œufs, le sperme, la laitance, les larves, le naissain et les stades juvéniles des poissons, des mollusques, des crustacés et des animaux marins.</li>
</ul>
</dd>
<dt>Poisson de fond : </dt><dd>Espèces (aussi appelées poissons démersaux) qui vivent et se nourrissent sur le fond marin ou à proximité. </dd>
<dt>Population : </dt><dd>Groupe d’individus de la même espèce qui occupent un espace donné et qui se reproduisent entre eux.</dd>
<dt>Previsions : </dt><dd> Prévoir le scénario le plus probable.</dd>
<dt>Prises accessoires : </dt><dd>Prises accidentelles d’une espèce dans une pêche qui en cible une autre. La partie conservée des prises accessoires est appelée prises fortuites, et la partie rejetée est appelée rejets.</dd>
<dt>Probabilité : </dt><dd>La mesure dans laquelle un événement ou un scénario est susceptible de se produire.</dd>
<dt>Productivité : </dt><dd>Le taux aux auquel la biomasse est produite dans un stock.</dd>
<dt>Projections : </dt><dd>Explorer et comparer les résultats de nombreux scénarios qui pourraient se produire.</dd>
<dt>Quota : </dt><dd>Voir la définition du <a href="#tac">total autorisé des captures</a> (TAC).</dd>
<dt>Ratio du potentiel du reproducteur : </dt><dd>Nombre d’œufs produits par un stock de poissons dans certaines conditions de pêche par rapport au nombre d’œufs qui seraient produits s’il n’était pas exploité.</dd>
<dt>Recrutement : </dt><dd> Le recrutement a lieu lorsque de nouveaux poissons sont ajoutés à une population. Il est mesuré lorsque des poissons petits ou jeunes sont assez grands ou assez vieux pour être capturés dans une pêcherie (qui peut être affectée par les limites de capture ou le type d'engin) ou dans une étude scientifique. </dd>
<dt>Relations stock-recrutement : </dt><dd>Les relations entre l’abondance ou la biomasse des adultes reproducteurs d’un stock et le nombre prévu de recrues qu’ils peuvent produire chaque année.</dd>
<dt>Rendement maximal durable (RMD) : </dt><dd>La prise annuelle moyenne maximale qui peut être prélevée d’un stock sur une période indéfinie dans les conditions environnementales actuelles.</dd>
<ul>
  <li><dt> B<sub>RMD</sub> : </dt><dd>La biomasse du stock qui produit le rendement maximal durable est appelée la B<sub>RMD</sub>.</dd></li>
</ul>

<dt>Recrutement non exploité : </dt><dd>Recrutement prévu si le stock de poissons n’est pas exploité.</dd>
<dt>Rendement par recrue : </dt><dd>La prise attendue en poids produite sur toute la durée de vie d’un poisson recruté dans le stock.</dd>
<dt>Sélectivité : </dt><dd>La probabilité de rétention une fois qu'un poisson entre dans l'engin de pêche). La sélectivité de l'engin est influencée par sa conception (type et taille des mailles, panneaux d'échappement, taille des hameçons).</dd>
<dt>Senne : </dt><dd>Filet de pêche qui pend verticalement dans l’eau et est muni de flotteurs en haut et de lests en bas; les extrémités sont rapprochées pour encercler les poissons.</dd>
<dt>Stock : </dt><dd>Groupe de poissons de la même espèce ayant un ou plusieurs attributs communs qui sont d’intérêts pour les gestionnaires.</dd>
<dt>Tableaux de décision : </dt><dd> Moyen simple de presenter des avis sur les consequences et les compromise possibles qui accompagnent différentes décisions.</dd>
<dt>Taux de variation : </dt><dd>Mesure de la survie des poissons juvéniles lorsqu’il y a peu ou pas de concurrence pour les ressources (aussi appelé compensation du recrutement).</dd>
<dt>Taux d’exploitation de référence : </dt><dd>Taux d’exploitation maximal du stock de poissons.</dd>
<a id="tac"></a>
<dt>Total autorisé des captures (TAC) : </dt><dd>Limite de prises établie pour une pêche donnée.</dd>
<dt>Unité de conservation (UC) : </dt><dd>Groupe de poissons suffisamment isolé des autres groupes qui, s’il disparaît localement, est très peu susceptible de recoloniser la zone naturellement.</dd>
<dt>Zone de gestion des stocks (ZGS) : </dt><dd>Des ensembles de stocks qui sont gérés conjointement avec un état du stock commun.</dd>




  </dl>



