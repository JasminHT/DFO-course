<p>Les avis scientifiques sont produits à partir des évaluations des stocks pour étayer la prise de décisions. Les avis scientifiques peuvent comprendre des renseignements sur <span class="gc-dwnld-info"><abbr title="L’état du stock : fait référence à une décision prise, sur la base des résultats de l’évaluation du stock, au sujet de l’état actuel du stock.">l’état du stock</abbr></span> et les conséquences potentielles de la prise de différentes mesures de gestion pour maintenir ou rétablir le stock au besoin. Les décisions sur la gestion des pêches sont prises en intégrant les avis scientifiques aux éléments suivants :</p>
<ul>
  <li>considérations socio-économiques&nbsp;;</li>
  <li>politiques sur la pêche&nbsp;;</li>
  <li>mobilisation des <span class="gc-dwnld-info"><abbr title="Détenteurs de droits: les successeurs modernes des collectivités autochtones qui ont signé des traités, qui ont occupé et utilisé les terres et se sont livrés à des pratiques historiques qui permettent de conclure à l’existence d’un droit ou d’un titre ancestral.">détenteurs de droits autochtones</abbr></span> et des intervenants&nbsp;;</li>
  <li>les Modes de connaissance autochtones&nbsp;;</li>
  <li>autres considérations culturelles.</li>

</ul>

<p>Le <a href="https://www.dfo-mpo.gc.ca/csas-sccs/index-fra.htm">Secrétariat canadien des avis scientifiques</a> a été créé en 1997 pour fournir des avis sur l’état et les tendances des stocks à l’appui de la gestion des pêcheries commerciales. En 2001, le secrétariat a été rebaptisé « Secrétariat canadien des avis scientifiques (SCAS) » et est chargé de coordonner l’élaboration des avis scientifiques du MPO examinés par les pairs, y compris les résultats des évaluations des stocks. La science de grande qualité développée lors du processus d’examen par les pairs du SCAS assure que les consultations des détenteurs de droits autochtones, des intervenants et des organismes consultatifs se déroulent à partir d’une base d’information partagée et fiable. L’intégrité et l’objectivité scientifiques sont des éléments essentiels de toutes les étapes du processus du SCAS, notamment : </p>

<ul>
<li>planification&nbsp;;</li>
<li>élaboration de documents scientifiques&nbsp;;</li>
<li>réunions d’examen par les pairs&nbsp;;</li>
<li>diffusion des avis scientifiques.</li>
</ul>

<figure>
  <img src="../images/fingers-on-keyboard.png">
  <figcaption>Source de la photo : Shutterstock
</figcaption>
</figure>

<p>Les documents du SCAS qui ont été examinés par des pairs sont publiés en ligne. Il en existe actuellement 3 séries de publications du SCAS :</p>
<ol>
<li>les avis scientifiques présentent l’avis scientifique passé en revue par des pairs et formulé de façon consensuelle&nbsp;;</li>
<li>les documents de recherche présentent les études et les analyses scientifiques examinées par des pairs. Les documents de recherche sont fondés sur les documents de travail produits et distribués pour examen avant les réunions d’examen par les pairs&nbsp;;</li>
<li>les réponses des Sciences présentent les avis scientifiques passés en revue par des pairs pendant des processus urgents et imprévus du SCAS ou lorsqu’une réunion consultative complète n’est pas nécessaire parce que la question peut être traitée par l’application d’un cadre consultatif existant.</li>

</ol>

