<p>Les évaluations intégrées des stocks combinent divers types de données (comme la composition selon la longueur et l’âge du stock, les indicateurs ou les indices de l’abondance actuelle et historique, les prises) dans un cadre de différents modèles qui fournissent des pièces précises d’un casse-tête plus vaste.</p>

