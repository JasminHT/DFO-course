  <details id="inline-captions">
    <summary>Modèles de production excédentaire</summary>  
    <p>Les <strong>modèles de production excédentaire</strong> (ou de dynamique de la biomasse) supposent que :</p>
    <ul>
      <li>la taille du stock est limitée par des facteurs environnementaux et la disponibilité des ressources (p. ex. nourriture, habitat, partenaires), aussi appelée <b>capacité de charge (K)</b>&nbsp;;</li>
      <li>les poissons produisent plus de progéniture que nécessaire pour reconstituer le stock&nbsp;;</li>
      <li>le prélèvement des poissons favorise la productivité en augmentant la disponibilité des ressources&nbsp;;</li>
      <li>cette productivité accrue peut créer un surplus de poissons dans un stock qui peut être récolté sans nuire à la durabilité du stock au fil du temps.</li>

    </ul>

    <p>Schaefer, Fox et Pella-Tomlinson sont des modèles courants de production excédentaire.</p>

