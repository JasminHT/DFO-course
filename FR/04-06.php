<aside class='bg-info'>
  <h4>Définition : </h4>
  <p>La discussion et le débat sont des éléments importants du processus d’examen par les pairs. Le <strong>consensus</strong> est l’absence d’opposition aux conclusions et aux avis de la réunion d’examen par les pairs. Le consensus repose sur des données scientifiques et de l’information, et non sur des considérations externes, comme les impacts potentiels de décisions futures.</p></aside>

<p>On parvient à un consensus en tenant compte d’autres interprétations et d’opinions contraires. Le partage d’opinions divergentes est encouragé parce que les avis scientifiques bénéficieront de leur contribution. Dans de nombreux cas, un consensus peut être atteint lorsque les participants estiment que des données supplémentaires ou des analyses plus approfondies pourraient soutenir une autre conclusion, mais qu’ils ne s’opposent pas aux conclusions proposées.</p>

<p>Il incombe aux présidents de la réunion de rappeler aux participants que les conclusions et les avis doivent être fondés sur des preuves ou des faits démontrés. Lorsqu’il est évident que des conclusions différentes sont appuyées par diverses sources de données ou par des analyses scientifiques différentes, mais valides, le consensus doit alors s’articuler sur la valeur de la preuve pour chaque conclusion proposée. </p>

<p>Si des conclusions différentes sont appuyées de façon équivalente par des données et des avis et sont tout aussi plausibles, ces informations sont incluses dans une section de l’avis intitulée « Autres interprétations ou informations ». Des conclusions tout aussi plausibles et d’autres perspectives fondées sur des données probantes qui ne font pas partie de l’avis final peuvent être formulées dans les rapports d’avis scientifiques. </p>



