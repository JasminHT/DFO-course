<h2>Unité 1.2 : Qu’est-ce que l’évaluation des stocks?</h2>
<p>Les <b>relations stock-recrutement</b> sont souvent importantes pour les évaluations des stocks. Il s’agit des relations entre <span class="gc-dwnld-info"><abbr title="Abondance (N) : Nombre total de poissons dans un stock.">l’abondance</abbr></span> ou la <span class="gc-dwnld-info"><abbr title="Biomasse (B) : Le poids total de tous les poissons d’un stock.">biomasse</abbr></span> des adultes reproducteurs d’un stock et le nombre prévu de recrues qu’ils peuvent produire chaque année. Autrement dit, le nombre de jeunes poissons dans un stock dépend du nombre de poissons adultes qui peuvent se reproduire chaque année. Dans la mesure du possible, ces relations peuvent contribuer à déterminer le niveau auquel un stock de poissons peut être pêché (c.-à-d. le nombre de poissons qu’il est possible de capturer et le nombre de poissons qu’il faut laisser dans l’eau pour la reproduction). </p>

<p>Des renseignements peuvent aider à déterminer la relation stock-recrutement d’un stock, notamment :</p>


<span class="gc-dwnld-info"><abbr title="">   </abbr></span>



<div class='collapsible blue' id="module2">
  <details>
    <summary><strong>Taux de variation ou compensation du recrutement</strong></summary>
    <p>Le <strong>taux de variation ou compensation du recrutement</strong> est la mesure de la survie des poissons juvéniles lorsqu’il y a peu ou pas de concurrence pour les ressources (p. ex. nourriture ou habitat).</p>
  </details>
</div>


<div class='collapsible blue' id="module3">
  <details>
    <summary><strong>Ratio du potentiel reproducteur</strong></summary>
    <p>Le <strong>ratio du potentiel reproducteur</strong> est le nombre d’œufs produits par un stock de poissons dans certaines conditions de pêche par rapport au nombre d’œufs qui seraient produits s’il n’était pas exploité.</p>
  </details>
</div>

<div class='collapsible blue' id="module1">
  <details>
    <summary><strong>Rendement par recrue</strong></summary>
    <p>Le <strong>rendement par recrue</strong> est la prise attendue en poids produite sur toute la durée de vie d’un poisson recruté dans le stock.</p>
  </details>
</div>


<div class='collapsible blue' id="module4">
  <details>
    <summary><strong>Biomasse non exploitée</strong></summary>
    <p>La <strong>biomasse non exploitée</strong> est le poids total des poissons d’un stock lorsqu’il n’est pas exploité.</p>
  </details>
</div>


<div class='collapsible blue' id="module5">
  <details>
    <summary><strong>Recrutement non exploité</strong></summary>
    <p>Le <strong>recrutement non exploité</strong> est le recrutement prévu si le stock de poissons n’est pas exploité.</p>
  </details>
</div>


<div class='collapsible blue' id="module6">
  <details>
    <summary><strong>Appauvrissement</strong></summary>
    <p>L’<strong>appauvrissement</strong> est une mesure de la biomasse actuelle d’un stock par rapport à la biomasse moyenne non pêchée.</p>
  </details>
</div>


