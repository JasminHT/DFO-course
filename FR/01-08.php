    <p>Le MPO étant un ministère à vocation scientifique, <a href="https://www.dfo-mpo.gc.ca/about-notre-sujet/publications/policy-politiques/science-integrity-integrite-scientifique/index-fra.html"><b>l’intégrité scientifique</b></a> est essentielle à son travail et à celui de ses employés. L’intégrité scientifique est indispensable à : </p>

    <ul>
      <li>la planification et l’exécution de la recherche et de la surveillance&nbsp;;</li>
      <li>la production et l’application d’avis scientifiques&nbsp;;</li>
      <li>la prise de décisions.</li>

    </ul>

    <p>Le MPO reconnaît que la confiance des intervenants dans la recherche et les renseignements scientifiques fournis par les gouvernements dépend de l’intégrité du processus par lequel cette information est produite, gérée et communiquée. Il en va de même pour la confiance dans les processus décisionnels qui utilisent ces renseignements. Les scientifiques et les chercheurs du MPO doivent respecter les normes d’excellence acceptées par l’ensemble de la communauté scientifique et par les peuples Autochtones et s’y conformer. Les discussions découlant d’interprétations divergentes des données scientifiques et de recherche sont encouragées en tant que partie légitime et nécessaire du processus. </p>

    <p>La recherche scientifique, les produits connexes et les communications du MPO doivent être menés à l’abri de toute ingérence politique et commerciale et libres de l’influence des clients et des intervenants. </p>

    <p>En l’absence de raisons claires et impérieuses de limiter la diffusion, l’information produite par le MPO est mise à la disposition du public en temps opportun, et elle représente fidèlement et reconnaît de façon appropriée les contributions des autres. </p>







