    <p>Le MPO reconnaît que l’inclusion des <b>Modes de connaissance autochtones</b> côte à côte et sur un pied d’égalité avec la science occidentale&nbsp;:</p>

    <ul><li>enrichit les connaissances et la compréhension collective des environnements marins, côtiers et d’eau douce&nbsp;;</li>
    <li>améliore le processus décisionnel du MPO&nbsp;;</li>
    <li>favorise l’établissement de relations respectueuses avec les peuples Autochtones&nbsp;;</li>
    <li>contribue à faire progresser la réconciliation en abordant les conséquences négatives de la colonisation.</li></ul>

    <aside class='bg-info'>
    <h4>Qu’entend-on par "Modes de connaissance autochtones" ?</h4>
    <p>Il n’existe pas de définition universelle des Modes de connaissance autochtones. Leur composition est déterminée par les Premières Nations, les Inuits et les Métis selon leurs propres termes. Les Modes de connaissance autochtones peut inclure les éléments suivants :</p>
    <ul>  
      <li>systèmes du savoir autochtones&nbsp;;</li>
      <li>science (p. ex. la recherche et la surveillance menées par les peuples Autochtones)&nbsp;;</li>
      <li>langues&nbsp;;</li>
      <li>visions du monde&nbsp;;</li>
      <li>valeurs autochtones.</li>

    </ul>
    </aside>

    <p>Les Modes de connaissance autochtones peuvent être partagés par les détenteurs de connaissances que la Nation, le gouvernement, la communauté ou l’organisation autochtone juge appropriés, notamment les personnes autochtones suivantes :  </p>
    <ul>
      <li>Aînés&nbsp;;</li>
      <li>spécialistes&nbsp;;</li>
      <li>pêcheurs&nbsp;;</li>
      <li>jeunes&nbsp;;</li>
      <li>autres Gardiens du savoir.</li>

    </ul>

    <p>La participation des peuples Autochtones, la collaboration avec eux et l’inclusion des Modes de connaissance autochtones sont encouragées à chaque étape du processus d’évaluation des stocks, de la collecte des données jusqu’à la production des avis scientifiques. Les peuples Autochtones déterminent quels Modes de connaissance autochtones sont pertinents pour un processus donné et comment et quand les communiquer au MPO. </p>

    <p>Les points d’entrée communs et l’inclusion des Modes de connaissance autochtones dans le processus d’évaluation des stocks comprennent : </p>
    <ul>
      <li>l’élaboration conjointe de questions de recherche d’intérêt mutuel&nbsp;;</li>
      <li>la collaboration des équipes de recherche&nbsp;;</li>
      <li>les activités de recherche et de surveillance menées par les peuples Autochtones&nbsp;;</li>
      <li>la participation d’experts Autochtones aux processus d’évaluation par les pairs du Secrétariat canadien de consultation scientifique (SCAS).</li>

    </ul>


  <p>Bien que les renseignements socio-économiques autochtones ne soient pas intégrés aux évaluations scientifiques des stocks, conformément à la section 2.5(d) de la  <a href="https://laws-lois.justice.gc.ca/fra/lois/f-14/page-1.html"><i>Loi sur les pêches</i></a>, ils peuvent être pris en compte dans le processus décisionnel et dans les résultats de l’évaluation des stocks.</p>




  




