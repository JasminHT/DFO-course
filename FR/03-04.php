<h2>Unité 3.2 : Évaluation de la stratégie de gestion</h2>

<figure>
  <img src="../images/airplane-pilots.jpg">
  <figcaption>Source de la photo : Shutterstock
</figcaption>
</figure>


<p><b>L’évaluation de la stratégie de gestion (ESG)</b> est une approche structurée de la gestion durable des pêches. C’est comme un simulateur de vol pour la pêche : il teste différents scénarios de pêche afin de déterminer comment capturer le poisson de manière durable, à l’instar des pilotes qui s’exercent au pilotage.</p>

<p>Imaginez un pilote d’avion : son objectif est d’atteindre leur destination en toute sécurité, même en cas de turbulences et de mauvais temps. Pour l’ESG : </p>
<ul>
<li>« l’avion » est la stratégie de gestion de la pêche ;</li>
<li>les « conditions météorologiques » sont le stock de poisson, la pêcherie et l'environnement ;</li>
<li>la « destination » est la réalisation d’objectifs prédéterminés pour la pêche.</li>
</ul>

<p>L’ESG peut s’appliquer à toute une série d’espèces, de cycles biologiques, <span class="gc-dwnld-info"><abbr title="L’état du stock : fait référence à une décision prise, sur la base des résultats de l’évaluation du stock, au sujet de l’état actuel du stock.">d’états des stocks</abbr></span> et de niveaux de disponibilité des données, et comprendre des contributions de la part des <span class="gc-dwnld-info"><abbr title="Détenteurs de droits: les successeurs modernes des collectivités autochtones qui ont signé des traités, qui ont occupé et utilisé les terres et se sont livrés à des pratiques historiques qui permettent de conclure à l’existence d’un droit ou d’un titre ancestral.">détenteurs de droits autochtones</abbr></span> et des intervenants.</p>

<p>Voici comment l’ESG fonctionne&nbsp;:</p>

<div class='collapsible blue' id="part1">
  <details>
    <summary>1. Définir le contexte décisionnel et fixer des objectifs pour la pêche </summary>
    <p>Les questions clés qui guident la définition du contexte décisionnel sont les suivantes :</p>
    <ul>
      <li>En quoi consiste la décision à prendre&nbsp;?</li>
      <li>Quel est le délai pour prendre la décision&nbsp;?</li>
      <li>À quelle fréquence la décision sera-t-elle évaluée et mise à jour&nbsp;?</li>
      <li>Quelles sont les exigences législatives et politiques&nbsp;?</li>
      <li>Quels sont les rôles et les responsabilités précises des parties concernées&nbsp;?</li>
    </ul>
    <p>Un exemple d’objectif pour un pilote pourrait être de faire atterrir l’avion avec la moitié du réservoir de carburant restant. Des exemples d’objectifs d’ESG pourrait être de maintenir le stock de poissons au-dessus de son <span class="gc-dwnld-info"><abbr title="Point de référence limite (PRL) : L’état d’un stock au-dessous duquel ce dernier subit de graves dommages.">point de référence limite</abbr></span> ou de capturer une certaine quantité de poissons chaque année. </p>

    <p>Si plusieurs objectifs sont fixés pour une pêche, il peut être impossible de les atteindre tous en même temps. Des compromis pourraient être requis.</p>
  </details>
</div>

<div class='collapsible blue' id="part2">
  <details>
    <summary>2. Créer des modèles opérationnels</summary>
    <p>Les <b>modèles opérationnels</b> sont des modèles informatiques qui simulent la croissance, la reproduction et l’interaction des stocks de poissons avec l’environnement. Ils peuvent simuler les changements environnementaux au fil du temps et la manière dont divers aspects d'une pêcherie peuvent affecter les stocks, comme la pêche dans des lieux spécifiques ou avec différents types d'engins.   </p>
 
    <p>Tout comme les conditions météorologiques dans un simulateur de vol, les interactions entre les stocks de poissons, les pêcheries et l'environnement varient dans le temps et ne sont pas faciles à prévoir. Plusieurs modèles opérationnels peuvent être élaborés pour représenter ces différents scénarios.</p>
  </details>
</div>

<div class='collapsible blue' id="part3">
  <details>
    <summary>3. Tester différentes stratégies de gestion</summary>
    <p>Les pilotes s’entraînent à piloter leur avion dans différentes conditions de vol, comme les turbulences et le bon ou le mauvais temps. Cela leur permet d'essayer différentes trajectoires de vol ou altitudes pour voir combien de carburant il reste à l'atterrissage.</p>

    <p>Pour l’ESG, les stratégies de gestion sont testées en ajustant des facteurs tels que les limites de capture, les saisons de pêche et les types d’engins afin de voir dans quelle mesure ces différents scénarios permettent d'atteindre les objectifs en matière de pêche.</p>
  </details>
</div>

<div class='collapsible blue' id="part4">
  <details>
    <summary>4. Évaluer l’incidence des différentes stratégies de gestion</summary>
    <p>Après que l’on a effectué des simulations, il est possible de voir dans quelle mesure les stratégies de gestion répondent aux objectifs fixés à l’étape 1. </p>

    <p>Dans un simulateur de vol, les pilotes utilisent différentes trajectoires de vol pour mesurer comment leurs choix affectent les mesures de performance telles que la consommation de carburant tout en arrivant à temps et en toute sécurité. De la même manière, la quantité de prises ou l'état d'un stock peuvent être utilisés comme mesures de performance pour l'ESG.</p>
  </details>
</div>

<div class='collapsible blue' id="part5">
  <details>
    <summary>5. Choisir et mettre en œuvre une stratégie de gestion</summary>
    <p>La stratégie de gestion qui répond le mieux aux objectifs des simulations peut être mise en pratique. Toutefois, il n'est pas toujours facile de déterminer ce qui est «&nbsp;le mieux&nbsp;». Différentes stratégies de gestion permettront d'atteindre certains objectifs mieux que d'autres, de sorte qu'il peut y avoir des compromis. Par exemple, les stratégies de gestion les plus susceptibles d'aboutir à des captures élevées peuvent aussi être plus susceptibles d'entraîner l'épuisement des stocks de poissons en deçà de leur point de référence limite.  </p>
 
    <p>Les gestionnaires de la pêche doivent tenir compte de l'importance des différents objectifs et des compromis et risques acceptables lorsqu'ils choisissent une stratégie de gestion. </p>

    <p>Une fois mise en œuvre, la stratégie de gestion choisie doit être surveillée pour qu’on s’assure qu’elle fonctionne bien dans la réalité.</p>
  </details>
</div>

<div class='collapsible blue' id="part6">
  <details>
    <summary>6. Répéter afin d’intégrer de nouveaux renseignements</summary>
    <p>L’ESG est un processus itératif et adaptatif. Les résultats des mesures de gestion peuvent permettre d’affiner ultérieurement les objectifs et les stratégies de gestion des pêches. De cette manière, la gestion des pêches est améliorée au fil du temps et adaptée au fur et à mesure que les connaissances s’accumulent. Une participation continue avec les détenteurs de droits autochtones et les intervenants peut s’avérer nécessaire.</p>
  </details>
</div>


