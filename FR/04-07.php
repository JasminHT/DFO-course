<h2>Unité 4.4 : Conflits d’intérêts</h2>

<aside class='bg-info'>
  <h4>Définition : </h4>
<p>Un <b>conflit d’intérêts</b> est un intérêt du participant susceptible d’influer, ou d’être perçu comme influant, sur son objectivité et son indépendance lorsqu’il participe au processus d’examen par les pairs.  </p>
</aside>

<p>Les conflits d’intérêts peuvent découler, entre autres :</p>
<ul>
<li>d’intérêts financiers et d’investissements, incluant les contributions de donateurs&nbsp;;</li>
<li>d’un lien avec un employeur&nbsp;;</li>
<li>d’une entente de consultation&nbsp;;</li>
<li>de subventions ou de contrats actuels ou anticipés&nbsp;;</li>
<li>de relations commerciales ou personnelles avec des personnes qui ont un intérêt concret dans des activités connexes, des organisations intéressées ou la fonction publique fédérale&nbsp;;</li>
<li>d’une position politique ou stratégique pertinente pour le résultat du processus.</li>

</ul>

<h3>Un conflit d’intérêts peut être : </h3>
<div class='collapsible blue' id="module1">
  <details>
    <summary>Réel  </summary>
      <p>il existe réellement</p>

  </details>
</div>
<div class='collapsible blue' id="module2">
  <details>
    <summary>Perçu </summary>
      <p>apparent pour un observateur</p>

  </details>
</div>
<div class='collapsible blue' id="module3">
  <details>
    <summary>Potentiel </summary>
      <p>on peut raisonnablement envisager qu’il existera dans l’avenir</p>
  </details>
</div>

<p>Les participants invités du MPO doivent signaler à leur superviseur et aux présidents de la réunion tout conflit d’intérêts dans lequel ils pourraient se trouver avant et durant leur participation. On déterminera ensuite si l’employé peut demeurer objectif tout au long du processus de consultation scientifique. </p>

<p>Les participants externes invités doivent déterminer eux-mêmes si un conflit réel, perçu ou potentiel dans lequel ils pourraient se trouver peut être géré et s’ils peuvent demeurer objectifs pendant le processus du SCAS. </p>

<p>Les présidents des réunions ou les coordonnateurs du SCAS, en tenant compte de l’avis du comité directeur, peuvent déterminer si une mesure est justifiée en réaction à un conflit d’intérêts réel, perçu ou potentiel.</p>



