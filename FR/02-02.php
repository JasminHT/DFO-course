<p>Dans la mesure du possible, les indices suivants peuvent être estimés à partir de données dépendantes de la pêche&nbsp;: </p>
<ul>
<li><span class="gc-dwnld-info"><abbr title="Capture par unité d’effort : quantité de poissons capturés avec une unité standard d’effort de pêche (par exemple, le nombre de poissons capturés pour 1000 hameçons par jour ou le poids des poissons capturés par heure de chalutage). La CPUE est souvent considérée comme un indice d’abondance relative (c’est-à-dire qu’elle est proportionnelle à la part de la biomasse (ou des effectifs) du stock qui est vulnérable à l’engin de pêche).">capture par unité d’effort</abbr></span>&nbsp;;</li>
<li> <span class="gc-dwnld-info"><abbr title="Abondance (N) : Nombre total de poissons dans un stock.">abondance</abbr></span>, <span class="gc-dwnld-info"><abbr title="Biomasse (B) : Le poids total de tous les poissons d’un stock.">biomasse</abbr></span>, et <span class="gc-dwnld-info"><abbr title="Biomasse du stock reproducteur (BSR) : Poids total de tous les poissons d’un stock qui ont atteint la maturité de reproduction.">la biomasse du stock reproducteur</abbr></span>&nbsp;;</li>
<li>paramètres de la population (p.ex. structure en âge et le ratio femelles-mâles)&nbsp;;</li>
<li><span class="gc-dwnld-info"><abbr title="Recrutement : Le recrutement a lieu lorsque de nouveaux poissons sont ajoutés à une population. Il est mesuré lorsque des poissons petits ou jeunes sont assez grands ou assez vieux pour être capturés dans une pêcherie (qui peut être affectée par les limites de capture ou le type d'engin) ou dans une étude scientifique. ">recrutement</abbr></span>&nbsp;;</li>
<li>paramètres de la condition de chaque poisson (p. ex. rapports poids-longueur)&nbsp;;</li>
<li><span class="gc-dwnld-info"><abbr title="Mortalité par pêche : Mesure des mortalités dans un stock attribuables à toutes les activités de pêche, y compris la pêche ciblée, les prises accessoires et les autres mortalités fortuites dans d’autres pêches.">mortalité par la pêche</abbr></span>&nbsp;;</li>




<li><span class="gc-dwnld-info"><abbr title="Sélectivité de l’engin : Taille ou âge auquel les poissons sont vulnérables à l’engin de pêche utilisé.">sélectivité des engins</abbr></span>.</li>
</ul>
<p>Les données dépendantes de la pêche peuvent être recueillies au fil du temps afin de produire des séries chronologiques de données utiles pour l’évaluation des stocks. Par exemple, l’information sur les prises totales dans le temps est un intrant courant et clé de nombreux modèles d’évaluation des stocks.</p>


