<aside class='bg-info'>
<p>Les données scientifiques utilisées dans les évaluations des stocks peuvent provenir de diverses sources qui sont regroupées en 2 catégories :</p>
<ul>
  <li>les <strong>données dépendantes de la pêche</strong> sont recueillies directement auprès des pêches commerciales, récréatives ou autochtones à des fins alimentaires, sociales et rituelles (ASR)&nbsp;;</li>
  <li>les <strong>données indépendantes de la pêche</strong> sont recueillies en dehors des pêches.</li>
</ul>
</aside>
<p>Chaque catégorie joue un rôle important et complémentaire dans l’évaluation des stocks.</p>


<aside class='bg-warning'>

  <p>La principale différence entre les 2 catégories de données est la suivante : </p>
  <ul>
    <li>les données dépendantes de la pêche sont recueillies là où la pêche a lieu&nbsp;;</li>
    <li>les données indépendantes de la pêche sont recueillies dans des zones géographiques plus vastes.</li>
  </ul>



</aside>


  <p>Les emplacements pour l’échantillonnage indépendant de la pêche sont souvent sélectionnés selon une approche stratifiée aléatoire : une zone géographique est divisée en sections (appelées strates) en fonction de certaines caractéristiques (p. ex. kilomètres carrés, profondeur). Les strates peuvent être choisies au hasard pour refléter plus précisément l’ensemble du stock dans les données. </p>
  
<p>Les données dépendantes de la pêche recueillies par les pêches commerciales, récréatives et autochtones à des fins alimentaires, sociales et rituelles (ASR) comprennent : </p>
<ul>
  <li>prises&nbsp;;</li>
  <li> <span class="gc-dwnld-info"><abbr title="Effort de pêche : Une mesure de l’activité de pêche utilisant des paramètres comme : le nombre de filets ou de lignes utilisés ; le nombre de bateaux de pêche déployés ; le nombre d’heures de pêche.">effort de pêche</abbr></span>&nbsp;;</li>
  <li>taille (longueur et poids), nombre et espèces des poissons capturés&nbsp;;</li>
  <li><span class="gc-dwnld-info"><abbr title="Prises accessoires : Prises accidentelles d’une espèce dans une pêche qui en cible une autre. La partie conservée des prises accessoires est appelée prises fortuites, et la partie rejetée est appelée rejets.">prises accessoires</abbr></span> et remises.</li>
</ul>


<p>Les sources de données dépendantes de la pêche collectées par les pêcheries commerciales, récréatives et ASR peuvent inclure :</p>
<ul>
<li>les registres de débarquement&nbsp;;</li>
<li>l’échantillonnage commercial&nbsp;;</li>
<li>les journaux de bord.</li>
</ul>


