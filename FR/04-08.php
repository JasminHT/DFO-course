<li>Le Secrétariat canadien des avis scientifiques (SCAS) coordonne l’élaboration des avis scientifiques pour le MPO.</li>

<li>Les réunions d’examen par les pairs sont le lieu de l’examen technique et de la remise en question de l’information scientifique. Elles mènent à des avis scientifiques fondés sur le consensus. Les réunions d’examen par les pairs ont pour objectif de fournir des avis scientifiques rigoureux, objectifs et impartiaux. Il ne s’agit pas de consultations.</li>

<li>Le consensus est l’absence d’opposition sur les conclusions et avis des réunions d’examen par les pairs avancés d’après des données et des informations scientifiques, et non d’après des considérations externes, comme les impacts potentiels de décisions futures.</li>

<li>Les participants aux réunions d’examen par les pairs doivent posséder l’expertise pertinente requise pour poser des questions de façon constructive, commenter et remettre en question l’information et les interprétations scientifiques présentées. Les participants doivent jouer un rôle actif pour parvenir à un consensus et doivent s’engager à participer en tant qu’experts objectifs.</li>

<li>On entend par conflit d’intérêts un intérêt du participant susceptible d’influencer, ou d’être perçu comme influençant, son objectivité et son indépendance lorsqu’il participe au processus d’examen par les pairs. Il est donc d’une importance capitale de détecter et de gérer les conflits d’intérêts réels, perçus ou potentiels tout au long du processus du SCAS.</li>

<li>Les publications du SCAS comprennent les avis scientifiques, les documents de recherche et les réponses des sciences.</li>




</ul>


