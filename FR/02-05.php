<h2>Unité 2.2 : Collecte de données</h2>

<p>Le MPO effectue régulièrement des relevés de surveillance pour recueillir des données indépendantes de la pêche. Par exemple, le MPO effectue des relevés multi-spécifiques qui couvrent une grande partie du plateau continental du Canada. </p>

<p>La surveillance entreprise par les <span class="gc-dwnld-info"><abbr title="Détenteurs de droits : les successeurs modernes des collectivités autochtones qui ont signé des traités, qui ont occupé et utilisé les terres et se sont livrés à des pratiques historiques qui permettent de conclure à l’existence d’un droit ou d’un titre ancestral.">détenteurs de droits</abbr></span> et les intervenants (tels que les peuples Autochtones et les industries de la pêche) au-delà de leurs activités de récolte est également une source essentielle de connaissances, d’expertise et de possibilités indépendantes des pêches au Canada. Les détenteurs de droits autochtones et les intervenants participent à de nombreux programmes de surveillance collaborative qui fournissent des données clés pour les évaluations des stocks du MPO. Les Modes de connaissance autochtones sont également holistiques et peuvent fournir un contexte important pour les évaluations des stocks (p. ex. conditions environnementales historiques, répartition historique des espèces, compréhension du cycle biologique des espèces).  </p>  

<p>Les pays étrangers effectuent également des relevés de surveillance qui guident les évaluations conjointes des stocks transfrontaliers ou chevauchants.  </p>

