<h2>Unité 2.2 : Collecte de données</h2>

<p>Les techniques de surveillance pour les relevés indépendants de la pêche comprennent : </p>
<ul>
  <li>la <span class="gc-dwnld-info"><abbr title="Chalutage : Méthode de pêche qui consiste à tirer derrière un ou plusieurs bateaux un filet de pêche lourdement lesté pour le maintenir sur le fond marin dans l’eau. Le filet utilisé pour le chalutage est appelé chalut.">pêche au chalut</abbr></span>, au
    <span class="gc-dwnld-info"><abbr title="Filet maillant : Filet de pêche qui pend verticalement de façon à ce que les poissons y restent piégés par leurs branchies.">filet maillant</abbr></span>, au <span class="gc-dwnld-info"><abbr title="Casier : Engins de pêche statiques qui fonctionnent passivement pour attraper ou piéger les poissons qui s'en approchent ou qui y pénètrent.">casier</abbr></span>
, à la 
   <span class="gc-dwnld-info"><abbr title="Dragage : Méthode de pêche dans laquelle une drague est traînée sur le fond marin, en le grattant ou en y pénétrant.">drague</abbr></span>, et à la
    <span class="gc-dwnld-info"><abbr title="Palangre : Type d’engin de pêche constitué d’une longue ligne maîtresse à laquelle sont pendues à intervalles réguliers des lignes plus courtes munies d’hameçons appâtés.">palangre</abbr></span>&nbsp;;</li>

  <li>la <span class="gc-dwnld-info"><abbr title="Pêche à l’électricité : Champ électrique émis par un générateur d’impulsions pour étourdir temporairement les poissons que l’on peut ensuite recueillir au moyen d’une épuisette.">pêche à l’électricité</abbr></span>&nbsp;;</li>
  <li>la collection d’œufs et de larves&nbsp;;</li>
  <li>visuels (par exemple, plongée sous-marine ou véhicules téléguidés (VTG))&nbsp;;</li>
  <li>les études <span class="gc-dwnld-info"><abbr title="Hydroacoustique : Étude du son dans l’eau. ">hydroacoustiques</abbr></span>&nbsp;;</li>
  <li>le marquage&nbsp;;</li>
  <li>des pêches expérimentales.</li>

</ul>

